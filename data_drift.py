@ensure_2d
def adversarial_validation(X_train, X_test, model=None, metric=roc_auc_score,random_state=42,n_splits=5):
    """
    Perform adversarial validation to detect distribution shift between X_train and X_test.
    Assigns label 0 to X_train, label 1 to X_test, then trains a classifier to predict the label.

    Parameters
    ----------
    X_train : array-like
        Training feature matrix.
    X_test : array-like
        Test feature matrix.
    model : sklearn-like estimator, optional
        Classifier with .fit() and .predict_proba().
    metric : callable, optional
        Scoring function (default is roc_auc_score).

    Returns
    -------
    results : dict
        Dictionary with average score and fold-level predictions.
    """
    # Create adversarial labels
    y_train_adv = np.zeros(len(X_train))
    y_test_adv = np.ones(len(X_test))

    X_adv = np.vstack((X_train, X_test))
    y_adv = np.concatenate((y_train_adv, y_test_adv))

    # Default model
    if model is None:
        model = LogisticRegression()

    prob_scores_lst = []
    pred_scores_lst = []
    scores = []
    models=[]

    skf = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=random_state)
    val_probs_all = np.full(len(X_adv), np.nan)  # to store out-of-fold probabilities
    val_preds_all = np.full(len(X_adv), np.nan)  # optional, for predicted classes

    for fold, (train_idx, val_idx) in enumerate(skf.split(X_adv, y_adv)):
        X_train_cv, X_val_cv = X_adv[train_idx], X_adv[val_idx]
        y_train_cv, y_val_cv = y_adv[train_idx], y_adv[val_idx]

        clf = model
        clf.fit(X_train_cv, y_train_cv)
        models.append(clf)

        val_probs = clf.predict_proba(X_val_cv)[:, 1]
        val_preds = clf.predict(X_val_cv)

        val_probs_all[val_idx] = val_probs
        val_preds_all[val_idx] = val_preds

        score = metric(y_val_cv, val_probs)
        scores.append(score)

    results = {
    'avg_score': np.mean(scores),
    'std_score': np.std(scores),
    'kurt_score': scipy.stats.kurtosis(scores),
    'skew_score': scipy.stats.skew(scores),
    'val_probs_all': val_probs_all,
    'val_preds_all': val_preds_all,
    'models': models
    }

    return results

from sklearn.base import clone

def adversarial_validation_with_permutation(
    x_train: np.ndarray | pd.DataFrame,
    x_test:  np.ndarray | pd.DataFrame,
    model,
    *,
    n_perm: int = 200,
    perm_random_state: int = 123,
    random_state: int = 42
):

    # Coerce to numpy for grouping; keep a DataFrame for the helper calls
    X_tr = x_train.values if isinstance(x_train, pd.DataFrame) else np.asarray(x_train)
    X_te = x_test.values  if isinstance(x_test,  pd.DataFrame) else np.asarray(x_test)
    X = np.vstack([X_tr, X_te])
    y = np.r_[np.zeros(len(X_tr), dtype=int), np.ones(len(X_te), dtype=int)]

    # ----- Observed (original grouping) -----
    obs = adversarial_validation(
        pd.DataFrame(X_tr),
        pd.DataFrame(X_te),
        model=clone(model),
        return_models=True
    )
    observed_score = float(obs["avg_score"])

    # Average feature importances across observed models (if supported)
    feat_imps = None
    models = obs.get("models", None)
    if models:
        imps = []
        for m in models:
            if hasattr(m, "feature_importances_"):
                imps.append(m.feature_importances_)
        if len(imps) > 0:
            feat_imps = np.mean(np.vstack(imps), axis=0)

    # ----- Permutation null -----
    rng = np.random.default_rng(perm_random_state)
    null_scores = []
    for _ in range(n_perm):
        y_perm = rng.permutation(y)
        # Rebuild groups by permuted labels
        X0 = pd.DataFrame(X[y_perm == 0])
        X1 = pd.DataFrame(X[y_perm == 1])

        res = adversarial_validation(
            X0, X1,
            model=clone(model)
        )
        null_scores.append(float(res["avg_score"]))

    null_scores = np.array(null_scores, dtype=float)
    # Guard in case all permutations were degenerate (should be rare)
    if null_scores.size == 0:
        return {
            "observed_score": observed_score,
            "null_scores": np.array([]),
            "p_value": 1.0,
            "null_mean": np.nan,-
            "null_std": np.nan,
            "z": np.nan,
            "feature_importances": feat_imps
        }

    # ----- Stats (two-sided around 0.5) -----
    dist_obs = abs(observed_score - 0.5)
    dist_null = np.abs(null_scores - 0.5)
    p_value = (np.sum(dist_null >= dist_obs) + 1) / (len(null_scores) + 1)

    null_mean = float(null_scores.mean())
    null_std  = float(null_scores.std(ddof=1))
    z = (observed_score - null_mean) / null_std if null_std > 0 else float("nan")
    ecdf = np.mean(null_scores <= observed_score)   # in [0, 1]

    drift_strength = 2.0 * abs(ecdf - 0.5)

    raw_effect = 2.0 * abs(observed_score - 0.5)  # 0–1
    composite = raw_effect * drift_strength       # 0–1

    return {
        "observed_score": observed_score,
        "null_scores": null_scores,
        "p_value": p_value,
        "null_mean": null_mean,
        "null_std": null_std,
        "z": z,
        "feature_importances": feat_imps,
        'ecdf': ecdf,
        'penalty': composite
    }

