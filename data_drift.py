def ensure_2d(func):
    @wraps(func)
    def wrapper(X_train, X_test, *args, **kwargs):
        # Convert pandas objects to numpy if needed
        if hasattr(X_train, "values"):
            X_train_arr = X_train.values
        else:
            X_train_arr = np.asarray(X_train)

        if hasattr(X_test, "values"):
            X_test_arr = X_test.values
        else:
            X_test_arr = np.asarray(X_test)

        # Enforce two dimensions
        if X_train_arr.ndim == 1:
            X_train_arr = X_train_arr.reshape(-1, 1)
        if X_test_arr.ndim == 1:
            X_test_arr = X_test_arr.reshape(-1, 1)

        return func(X_train_arr, X_test_arr, *args, **kwargs)
    return wrapper

