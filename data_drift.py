ArrayLike = Union[np.ndarray, pd.DataFrame]

def _to_numpy(X: ArrayLike) -> np.ndarray:
    return X.values if isinstance(X, pd.DataFrame) else np.asarray(X)

def _oof_if_scores(
    X: np.ndarray,
    splitter,
    if_kwargs: dict,
) -> Tuple[np.ndarray, list[IsolationForest]]:
    """
    Compute OOF score_samples for X using IsolationForest and return fitted fold models.
    """
    n = X.shape[0]
    oof_scores = np.empty(n, dtype=float)
    models = []
    for tr_idx, va_idx in splitter.split(X):
        model = IsolationForest(**if_kwargs)
        model.fit(X[tr_idx])
        oof_scores[va_idx] = model.score_samples(X[va_idx])
        models.append(model)
    return oof_scores, models

def _avg_scores_from_models(models: Iterable[IsolationForest], X: np.ndarray) -> np.ndarray:
    """Average score_samples across a list of fitted IsolationForest models."""
    scores = np.column_stack([m.score_samples(X) for m in models])
    return scores.mean(axis=1)

@dataclass
class IFDriftTriadCV(BaseEstimator, TransformerMixin):
    """
    Build 3 leakage-safe features from IsolationForest using CV:

    - score_train_model: scores from models trained only on train folds
    - score_test_model:  scores from models trained only on test folds
    - score_both_model:  OOF scores from CV over concatenated train+test

    Parameters
    ----------
    n_splits_train : int
        Folds for the 'train-only' models (default 5).
    n_splits_test : int
        Folds for the 'test-only' models (default 5).
    n_splits_both : int
        Folds for the 'both' (concatenated) models (default 5).
    splitter_train, splitter_test, splitter_both :
        Optional custom splitters with .split(X) (e.g., TimeSeriesSplit).
        If provided, they override the corresponding n_splits_*.
    if_kwargs : dict
        Keyword args passed to IsolationForest (e.g., n_estimators, random_state, n_jobs, contamination).
        Reasonable defaults are provided if omitted.
    return_pandas : bool
        If True, returns pandas DataFrames with the specified column names; else numpy arrays.

    Notes
    -----
    - No adversarial classifier here—just features.
    - Supports time-aware CV if you pass a TimeSeriesSplit (or your PurgedKFold) as splitters.
    - For cross-domain scoring:
        * Train-models score test by averaging predictions from each train-fold model.
        * Test-models score train by averaging predictions from each test-fold model.
    """
    n_splits_train: int = 5
    n_splits_test: int = 5
    n_splits_both: int = 5
    splitter_train: Optional[object] = None
    splitter_test: Optional[object] = None
    splitter_both: Optional[object] = None
    if_kwargs: Optional[dict] = None
    return_pandas: bool = True

    def __post_init__(self):
        if self.if_kwargs is None:
            # Sensible defaults; adjust as needed.
            self.if_kwargs = dict(
                n_estimators=300,
                max_samples="auto",
                contamination="auto",
                bootstrap=False,
                n_jobs=-1,
                random_state=42,
                verbose=0,
            )

    def _make_splitter(self, fallback_splits: int, provided) -> object:
        if provided is not None:
            return provided
        return KFold(n_splits=fallback_splits, shuffle=True, random_state=self.if_kwargs.get("random_state", 42))

    def fit_transform(
        self,
        X_train: ArrayLike,
        X_test: ArrayLike,
    ) -> Tuple[Union[pd.DataFrame, np.ndarray], Union[pd.DataFrame, np.ndarray]]:
        """
        Returns
        -------
        train_feats, test_feats
            Either DataFrames or ndarrays with columns (or order):
            ['score_train_model','score_test_model','score_both_model']
        """
        Xtr = _to_numpy(X_train)
        Xte = _to_numpy(X_test)

        # --- Splitters
        spl_train = self._make_splitter(self.n_splits_train, self.splitter_train)
        spl_test  = self._make_splitter(self.n_splits_test,  self.splitter_test)
        spl_both  = self._make_splitter(self.n_splits_both,  self.splitter_both)

        # --- 1) Train-only models: OOF for train, avg for test
        s_tr_oof, tr_models = _oof_if_scores(Xtr, spl_train, self.if_kwargs)
        s_tr_on_te = _avg_scores_from_models(tr_models, Xte)

        # --- 2) Test-only models: OOF for test, avg for train
        s_te_oof, te_models = _oof_if_scores(Xte, spl_test, self.if_kwargs)
        s_te_on_tr = _avg_scores_from_models(te_models, Xtr)

        # --- 3) Both (concatenated) models: OOF for all rows
        X_all = np.vstack([Xtr, Xte])
        s_both_oof_all, _ = _oof_if_scores(X_all, spl_both, self.if_kwargs)
        s_both_tr = s_both_oof_all[: Xtr.shape[0]]
        s_both_te = s_both_oof_all[Xtr.shape[0] :]

        # Assemble outputs
        train_cols = np.column_stack([s_tr_oof, s_te_on_tr, s_both_tr])
        test_cols  = np.column_stack([s_tr_on_te, s_te_oof, s_both_te])

        if self.return_pandas:
            train_df = pd.DataFrame(train_cols, columns=["score_train_model","score_test_model","score_both_model"])
            test_df  = pd.DataFrame(test_cols,  columns=["score_train_model","score_test_model","score_both_model"])
            return train_df, test_df
        return train_cols, test_cols

