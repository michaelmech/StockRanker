def calc_max_min_seg_len(T: int, n_clusters: int) -> int:
    """
    Compute the maximum valid min_seg_len given:
      - T          : number of rows in eigvec_df
      - n_clusters : desired number of clusters

    Clustering is possible iff:
        ceil(T / min_seg_len) >= max(2, n_clusters)

    Returns
    -------
    int
        Maximum allowable min_seg_len (>= 1).

    Raises
    ------
    ValueError
        If it is mathematically impossible to satisfy the constraints
        (i.e., T too small for the requested n_clusters).
    """
    if T < 2:
        raise ValueError(f"T must be >= 2, got {T}.")

    B = max(2, n_clusters)

    if B > T:
        raise ValueError(
            "Impossible clustering configuration.\n"
            f"  T          : {T}\n"
            f"  n_clusters : {n_clusters}\n\n"
            "Need at least T >= max(2, n_clusters)."
        )

    return (T - 1) // (B - 1)

def build_regimes_from_clustering(
    eigvec_df: pd.DataFrame,
    n_clusters: int,
    min_seg_len: int = 'auto',
):
    """
    Create regime definitions with a minimum segment length by clustering
    over block representatives (no averaging).
    """

    # Index must be datetime
    dates = pd.to_datetime(eigvec_df.index)
    eigvec_df = eigvec_df.copy()
    eigvec_df.index = dates


    X = eigvec_df.fillna(-1).values
    T = X.shape[0]
    if T == 0:
        return []

    # ---------------------------------------------------------
    # 1) Create blocks of length >= min_seg_len
    # ---------------------------------------------------------
    T = len(eigvec_df)
    if min_seg_len=='auto':
      min_seg_len = calc_max_min_seg_len(T, n_clusters)

    block_ids = np.arange(T) // min_seg_len
    n_blocks = block_ids[-1] + 1

    B = max(2, n_clusters)
    max_min_seg_len = (T - 1) // (B - 1)

    assert 1 <= min_seg_len <= max_min_seg_len, (
        "Invalid min_seg_len.\n"
        f"  T (rows)           : {T}\n"
        f"  n_clusters         : {n_clusters}\n"
        f"  min_seg_len (given): {min_seg_len}\n\n"
        "Required bounds:\n"
        f"  1 <= min_seg_len <= {max_min_seg_len}\n"
    )

    # ---------------------------------------------------------
    # 2) Pick one representative from each block
    #    (first timestamp in each block)
    # ---------------------------------------------------------
    rep_indices = []
    for b in range(n_blocks):
        first_idx = np.where(block_ids == b)[0][0]
        rep_indices.append(first_idx)

    X_block = X[rep_indices]  # shape (n_blocks, D)

    # ---------------------------------------------------------
    # 3) Chain connectivity between neighboring blocks
    # ---------------------------------------------------------
    connectivity = diags(
        [1, 1],
        offsets=[-1, 1],
        shape=(n_blocks, n_blocks)
    )

    # ---------------------------------------------------------
    # 4) Cluster the representatives
    # ---------------------------------------------------------
    model = AgglomerativeClustering(
        n_clusters=n_clusters,
        linkage="ward",
        connectivity=connectivity
    )
    block_labels = model.fit_predict(X_block)

    # ---------------------------------------------------------
    # 5) Expand block labels back to the full timeline
    # ---------------------------------------------------------
    labels_full = block_labels[block_ids]

    # ---------------------------------------------------------
    # 6) Convert contiguous runs into regime tuples
    # ---------------------------------------------------------
    regimes = []
    current = labels_full[0]
    start_idx = 0
    regime_id = 0

    for i in range(1, T):
        if labels_full[i] != current:
            start = dates[start_idx]
            end = dates[i - 1]
            regimes.append((regime_id, start.strftime("%Y-%m-%d"), end.strftime("%Y-%m-%d")))
            regime_id += 1

            current = labels_full[i]
            start_idx = i

    # last regime
    start = dates[start_idx]
    end = dates[T - 1]
    regimes.append((regime_id, start.strftime("%Y-%m-%d"), end.strftime("%Y-%m-%d")))

    return regimes

from sklearn.ensemble import IsolationForest
from scipy.stats import ks_2samp
from numpy.random import choice

class IsolationForestSubSampler:
    """
    Subsamples rows using IsolationForest anomaly scores while preserving the
    distribution of anomaly scores, using the same logic as BorutaShap.find_sample.

    Workflow
    --------
    1. Fit an IsolationForest on X (optionally with sample_weight).
    2. Compute anomaly scores via score_samples(X).
    3. Iteratively:
        - Draw a random subset of size k (starting at ~10% of data).
        - Compare the distribution of scores in the subset vs. full set using KS test.
        - If p-value > ks_pvalue_threshold, accept that subset.
        - Otherwise, after max_draws_per_size tries, increase subset size by step_percent.
    4. Expose sampled indices and sampled data.
    """

    def __init__(self,
                 ks_pvalue_threshold: float = 0.95,
                 max_draws_per_size: int = 20,
                 step_percent: float = 5.0,
                 random_state: int | None = None,
                 isolation_forest_kwargs: dict | None = None):
        """
        Parameters
        ----------
        ks_pvalue_threshold : float
            P-value threshold for KS test between full and sample score distributions.
            Must be in (0, 1). Default is 0.95 (same as in BorutaShap).

        max_draws_per_size : int
            Number of random draws to attempt at a given sample size before
            increasing the sample size. Mirrors the `iteration == 20` logic.

        step_percent : float
            Step size in percent for candidate sample sizes. The original logic uses
            fixed 5% increments (5%, 10%, 15%, ...). Here we keep that default.

        random_state : int or None
            Random state passed to IsolationForest and used for sampling indices.

        isolation_forest_kwargs : dict or None
            Extra keyword arguments forwarded to IsolationForest.
        """
        if not (0.0 < ks_pvalue_threshold < 1.0):
            raise ValueError("ks_pvalue_threshold must be in (0, 1).")

        self.ks_pvalue_threshold = ks_pvalue_threshold
        self.max_draws_per_size = max_draws_per_size
        self.step_percent = step_percent
        self.random_state = random_state
        self.isolation_forest_kwargs = isolation_forest_kwargs or {}

        # Attributes filled after fit
        self.iforest_ = None
        self.scores_ = None           # anomaly scores for all rows
        self.sample_indices_ = None   # indices of chosen sample
        self.n_samples_ = None

    # ---------------------------------------------------------------------
    # Internal helpers – mirror the BorutaShap implementation
    # ---------------------------------------------------------------------
    @staticmethod
    def _get_percent(num, percent):
        """Generic percent helper (BorutaShap.get_5_percent is a special case)."""
        return round(percent / 100.0 * num)

    def _get_percent_splits(self, length: int):
        """
        Splits into step_percent intervals, e.g.
        5% increments: [5%, 10%, 15%, ...] * length

        Mirrors BorutaShap.get_5_percent_splits but with configurable percent.
        """
        if length <= 1:
            return np.array([length], dtype=int)

        base = self._get_percent(length, self.step_percent)
        base = max(base, 1)  # at least 1 row
        return np.arange(base, length, base)

    def _find_sample_indices(self) -> np.ndarray:
        """
        Implements the same sampling logic as BorutaShap.find_sample:

        - Compute candidate sizes: step_percent, 2*step_percent, ...
        - For each size, try 'max_draws_per_size' random subsamples.
        - Use KS-test on scores to check similarity.
        - Return indices of first subsample with KS p-value > threshold.
        """
        if self.scores_ is None:
            raise RuntimeError("You must call fit(X, ...) before sampling.")

        n = self.scores_.shape[0]
        if n == 0:
            raise ValueError("Cannot subsample an empty dataset.")

        # Candidate sizes in percent increments (as integers)
        size_candidates = self._get_percent_splits(n)
        if size_candidates.size == 0:
            # fallback: sample all rows
            return np.arange(n)

        # For reproducibility of choice()
        rng = np.random.default_rng(self.random_state)

        element = 1  # start at ~2 * step_percent, as in original code (index 1)
        iteration = 0

        # Ensure we start in-bounds
        element = min(element, size_candidates.size - 1)

        while True:
            current_size = size_candidates[element]

            # Draw candidate subsample
            sample_indices = rng.choice(
                np.arange(n),
                size=current_size,
                replace=False
            )
            sample_scores = self.scores_[sample_indices]

            # KS test between full and subsample distributions
            pvalue = ks_2samp(self.scores_, sample_scores).pvalue
            if pvalue > self.ks_pvalue_threshold:
                # Good subsample found
                return sample_indices

            iteration += 1

            # After 'max_draws_per_size' tries, move to a larger sample size
            if iteration >= self.max_draws_per_size:
                iteration = 0
                element += 1
                if element >= size_candidates.size:
                    # If we exhausted all sizes without passing the KS test,
                    # fallback to using the largest candidate size.
                    return rng.choice(
                        np.arange(n),
                        size=size_candidates[-1],
                        replace=False
                    )

    # ---------------------------------------------------------------------
    # Public API
    # ---------------------------------------------------------------------
    def fit(self, X, sample_weight=None):
        """
        Fit the IsolationForest on X and compute the subsample indices.

        Parameters
        ----------
        X : pandas.DataFrame or array-like, shape (n_samples, n_features)
            Input data.

        sample_weight : array-like, shape (n_samples,), optional
            Sample weights to pass to IsolationForest.

        Returns
        -------
        self
        """
        # Convert to array for IsolationForest, but keep original index
        if isinstance(X, pd.DataFrame):
            X_array = X.values
            self._index_ = X.index.to_numpy()
        else:
            X_array = np.asarray(X)
            self._index_ = np.arange(X_array.shape[0])

        self.n_samples_ = X_array.shape[0]

        # Fit IsolationForest using same pattern as BorutaShap.isolation_forest
        iforest_params = {
            "random_state": self.random_state,
            **self.isolation_forest_kwargs
        }
        self.iforest_ = IsolationForest(**iforest_params).fit(
            X_array,
            sample_weight=sample_weight
        )
        self.scores_ = self.iforest_.score_samples(X_array)

        # Find representative subsample indices (relative to original index)
        rel_indices = self._find_sample_indices()
        self.sample_indices_ = self._index_[rel_indices]

        return self

    def get_sample_indices(self):
        """
        Return the indices (matching the index of the fitted X if DataFrame)
        of the selected subsample.

        Returns
        -------
        np.ndarray
            1D array of indices.
        """
        if self.sample_indices_ is None:
            raise RuntimeError("Subsample not yet computed. Call fit(X, ...) first.")
        return self.sample_indices_

    def transform(self, X):
        """
        Return the subsampled X, using the indices learned in fit().

        Parameters
        ----------
        X : pandas.DataFrame or array-like
            Data to subsample. Must be aligned with what was passed to fit.

        Returns
        -------
        subsampled_X : same type as X
        """
        if self.sample_indices_ is None:
            raise RuntimeError("Subsample not yet computed. Call fit(X, ...) first.")

        if isinstance(X, pd.DataFrame):
            # Use index-based selection; assumes alignment with fit() call
            return X.loc[self.sample_indices_]
        else:
            X_array = np.asarray(X)
            # Here sample_indices_ are positional if X was an array at fit time
            # or label-based if X was a DataFrame. In the latter case, we stored
            # the original index values, so we need to map back to positions.
            if isinstance(getattr(self, "_index_", None), np.ndarray):
                # Map label indices to positions if needed
                if not np.array_equal(self._index_, np.arange(self._index_.size)):
                    # build mapping from label -> position
                    label_to_pos = {label: pos for pos, label in enumerate(self._index_)}
                    pos_indices = np.array([label_to_pos[i] for i in self.sample_indices_])
                    return X_array[pos_indices]
            return X_array[self.sample_indices_]

    def fit_transform(self, X, sample_weight=None):
        """
        Convenience method: fit and then transform.

        Parameters
        ----------
        X : pandas.DataFrame or array-like
        sample_weight : array-like, optional

        Returns
        -------
        subsampled_X : same type as X
        """
        return self.fit(X, sample_weight=sample_weight).transform(X)

from dataclasses import dataclass
