def _spearman_corr_df(X_df: pd.DataFrame) -> pd.DataFrame:
    """Spearman (via Pearson on ranks) with tie handling; symmetric DataFrame."""
    ranks = X_df.apply(
        lambda col: pd.Series(rankdata(col, method="average"), index=X_df.index),
        axis=0,
    )
    return ranks.corr(method="pearson")

def _to_distance_from_corr(corr: np.ndarray, absolute=True) -> np.ndarray:
    """Distance = |1 - corr| with NaNs→1.0, clipped to [0,2], zero diag."""
    C = np.array(corr, dtype=float)
    if absolute:
        C = np.abs(C)
    D = np.abs(1.0 - C)
    D = np.where(np.isnan(D), 1.0, D)
    D = np.clip(D, 0.0, 2.0)
    np.fill_diagonal(D, 0.0)
    return D


def _avg_intercluster_distance(D: np.ndarray, A: list[int], B: list[int]) -> float:
    """Average pairwise distance between two index sets A and B."""
    if len(A) == 0 or len(B) == 0:
        return np.inf
    return D[np.ix_(A, B)].mean()


def _merge_clusters_until_k(D: np.ndarray, clusters: list[list[int]], k: int) -> list[list[int]]:
    """
    Greedily merge the pair of clusters with the smallest average inter-cluster distance
    until len(clusters) <= k. Operates on index lists; returns new list of clusters.
    """
    if k is None or k < 1:
        return clusters
    clusters = [sorted(c) for c in clusters if len(c) > 0]
    if len(clusters) <= k:
        return clusters

    # Precompute a distance matrix between current clusters
    # We’ll rebuild rows/cols as merges happen (small N; features count drives earlier step anyway).
    while len(clusters) > k:
        n = len(clusters)
        best_i = best_j = None
        best_d = np.inf
        for i in range(n):
            for j in range(i + 1, n):
                d = _avg_intercluster_distance(D, clusters[i], clusters[j])
                if d < best_d:
                    best_d, best_i, best_j = d, i, j
        # Merge the closest pair
        merged = sorted(clusters[best_i] + clusters[best_j])
        # Rebuild cluster list
        new_clusters = []
        for t, cl in enumerate(clusters):
            if t not in (best_i, best_j):
                new_clusters.append(cl)
        new_clusters.append(merged)
        clusters = new_clusters
    return clusters

class HDBSCANClusterFeatureSelector(BaseEstimator, TransformerMixin):
    """
    Cluster features via HDBSCAN on a precomputed distance matrix built from
    Spearman rank correlation: D = |1 - rho_s|. Select one representative feature per cluster.

    New:
    ----
    max_k : int or None
        If provided, post-process clusters by greedily merging the closest clusters
        (average linkage on D) until the number of clusters <= max_k.

    Other params are unchanged from your previous class.
    """

    def __init__(
        self,
        min_cluster_size=None,
        min_samples=None,
        cluster_selection_method="eom",
        selection_strategy="medoid",
        scorer=None,
        absolute_correlation=True,
        metric="spearman",
        allow_noise=True,
        random_state=None,
        max_k=None,
    ):
        self.min_cluster_size = min_cluster_size
        self.min_samples = min_samples
        self.cluster_selection_method = cluster_selection_method
        self.selection_strategy = selection_strategy
        self.scorer = scorer
        self.absolute_correlation = absolute_correlation
        self.metric = metric
        self.allow_noise = allow_noise
        self.random_state = random_state
        self.max_k = max_k

    def fit(self, X, y=None):
        # Preserve DataFrame columns if present
        self.columns_ = list(X.columns) if isinstance(X, pd.DataFrame) else None
        X_df = pd.DataFrame(X) if not isinstance(X, pd.DataFrame) else X

        if self.metric != "spearman":
            raise ValueError("Only 'spearman' is supported per the project instructions.")

        # 1) Spearman correlation across features
        corr_df = _spearman_corr_df(X_df)

        # 2) Distance = |1 - rho|
        D = _to_distance_from_corr(corr_df.values, absolute=self.absolute_correlation)
        self.distance_ = D

        n_features = D.shape[0]
        mcs = self.min_cluster_size if self.min_cluster_size is not None else max(
            2, int(np.sqrt(n_features))
        )

        # 3) HDBSCAN clustering on precomputed distances (features as samples)
        clusterer = hdbscan.HDBSCAN(
            metric="precomputed",
            min_cluster_size=mcs,
            min_samples=self.min_samples,
            cluster_selection_method=self.cluster_selection_method,
            cluster_selection_epsilon=0.0,
            allow_single_cluster=False,
            core_dist_n_jobs=1,
            gen_min_span_tree=False,
        )
        base_labels = clusterer.fit_predict(D)

        # 4) Build initial cluster lists (include noise as singleton if allowed, else drop)
        clusters = {}
        for j, c in enumerate(base_labels):
            if c == -1 and not self.allow_noise:
                continue
            cid = c if c != -1 else f"noise_{j}"
            clusters.setdefault(cid, []).append(j)

        # Convert to list of index lists for max_k merging
        cluster_lists = list(clusters.values())

        # 5) Enforce max_k via greedy merging (if needed)
        if self.max_k is not None:
            cluster_lists = _merge_clusters_until_k(self.distance_, cluster_lists, int(self.max_k))

        # Reindex cluster ids to simple integers after merging
        self.clusters_ = {i: cl for i, cl in enumerate(cluster_lists)}

        # 6) Representative selection (medoid or scorer-based)
        self.medoids_ = {}
        reps = []
        n_features = D.shape[0]
        colnames = self.columns_ if self.columns_ is not None else list(range(n_features))

        for cid, idxs in self.clusters_.items():
            if len(idxs) == 1:
                rep = idxs[0]
                self.medoids_[cid] = rep
                reps.append(rep)
                continue

            if self.selection_strategy == "best_scorer" and callable(self.scorer) and y is not None:
                best_score, best_idx = -np.inf, None
                for j in idxs:
                    xj = X_df[colnames[j]].values
                    try:
                        s = float(self.scorer(xj, y))
                    except Exception:
                        s = -np.inf
                    if s > best_score:
                        best_score, best_idx = s, j
                rep = best_idx if best_idx is not None else idxs[0]
                # also compute medoid
                intra = D[np.ix_(idxs, idxs)]
                medoid = idxs[np.argmin(intra.mean(axis=1))]
                self.medoids_[cid] = medoid
                reps.append(rep)
            else:
                intra = D[np.ix_(idxs, idxs)]
                medoid = idxs[np.argmin(intra.mean(axis=1))]
                self.medoids_[cid] = medoid
                reps.append(medoid)

        # 7) Finalize selected features list
        if self.columns_ is not None:
            self.selected_features_ = [self.columns_[j] for j in reps]
        else:
            self.selected_features_ = reps

        # 8) Export labels_ compatible with sklearn convention (noise = -1)
        # Map features to cluster labels; merged clusters get new integer ids.
        labels = np.full(n_features, -1, dtype=object)
        for cid, idxs in self.clusters_.items():
            for j in idxs:
                labels[j] = cid
        self.labels_ = np.array(labels)

        return self

    def transform(self, X):
        check_is_fitted(self, "selected_features_")
        if isinstance(X, pd.DataFrame):
            return X.loc[:, self.selected_features_]
        if self.columns_ is None and all(isinstance(f, int) for f in self.selected_features_):
            return np.asarray(X)[:, self.selected_features_]
        X_df = pd.DataFrame(X, columns=self.columns_)
        return X_df.loc[:, self.selected_features_].values

    def fit_transform(self, X, y=None):
        return self.fit(X, y).transform(X)

    def cluster_summary(self):
        """List of dicts: {cluster_id, size, representative, members} (uses column names if available)."""
        check_is_fitted(self, "clusters_")
        out = []
        for cid, idxs in self.clusters_.items():
            rep_idx = self.medoids_.get(cid, idxs[0])
            if self.columns_ is not None:
                members = [self.columns_[j] for j in idxs]
                representative = self.columns_[rep_idx]
            else:
                members = idxs
                representative = rep_idx
            out.append(
                {
                    "cluster_id": cid,
                    "size": len(idxs),
                    "representative": representative,
                    "members": members,
                }
            )
        return out

