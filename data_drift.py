def _predict_proba_smart(model, X) -> np.ndarray:
    """
    Return 'probability of class 1' for a wide range of sklearn-like models.

    Preference order:
    1. model.predict_proba
       - If binary: return P(y=1)
       - If multiclass: try P(y=1), else max probability.
    2. model.decision_function
       - Convert scores to (0,1) via logistic transform for a 'probability-like' score.
    3. model.predict
       - Use hard labels (0/1) as degenerate probabilities.

    Always returns a 1D float array of shape (n_samples,).
    """

    X = np.asarray(X)

    # 1) True probabilities if available
    if hasattr(model, "predict_proba"):
        proba = np.asarray(model.predict_proba(X))

        # If already 1D, just ensure float
        if proba.ndim == 1:
            return proba.astype(float)

        n_classes = proba.shape[1]

        # Try to locate class "1" explicitly, if we know classes_
        if hasattr(model, "classes_"):
            classes = list(getattr(model, "classes_", []))
            if 1 in classes:
                pos_idx = classes.index(1)
                return proba[:, pos_idx].astype(float)

        # Fallbacks:
        if n_classes == 2:
            # Standard binary convention: column 1 is "positive"
            return proba[:, 1].astype(float)

        # Multiclass & no explicit class 1: use max probability as 'positiveness'
        return proba.max(axis=1).astype(float)

    # 2) Decision function (e.g., linear SVM, logistic regression w/o predict_proba)
    if hasattr(model, "decision_function"):
        scores = np.asarray(model.decision_function(X))

        # Binary: 1D decision scores
        if scores.ndim == 1:
            # Map scores to (0, 1) using logistic transform
            return 1.0 / (1.0 + np.exp(-scores))

        # Multiclass: scores shape (n_samples, n_classes)
        n_classes = scores.shape[1]
        if hasattr(model, "classes_"):
            classes = list(getattr(model, "classes_", []))
            if 1 in classes:
                pos_idx = classes.index(1)
                s = scores[:, pos_idx]
            elif n_classes == 2:
                # fall back to second column if binary
                s = scores[:, 1]
            else:
                # otherwise, take the max score as a proxy
                s = scores.max(axis=1)
        else:
            # No classes_: treat max as "positiveness"
            if n_classes == 2:
                s = scores[:, 1]
            else:
                s = scores.max(axis=1)

        return 1.0 / (1.0 + np.exp(-s))

    # 3) Fallback: just use predictions as probabilities (0 or 1)
    if hasattr(model, "predict"):
        preds = np.asarray(model.predict(X))

        if preds.ndim == 1:
            return preds.astype(float)

        # Occasionally predictors output one-hot-style arrays
        if preds.ndim == 2 and preds.shape[1] == 2:
            return preds[:, 1].astype(float)

    raise TypeError(
        f"Model of type {type(model)!r} does not support probability or score "
        "prediction via predict_proba, decision_function, or predict()."
    )


@ensure_2d
def av_tts(
    x_train: np.ndarray | pd.DataFrame,
    x_test:  np.ndarray | pd.DataFrame,
    model: Any | None = None,
    metric=roc_auc_score,
    test_size: float = 0.30,
    random_state: Optional[int] = 42,
    stratify: bool = True,
    feature_names: Optional[list[str]] = None,
    **kwargs: Any,  # absorb unused extras for compatibility
) -> dict:

    # Coerce to arrays, preserve/resolve feature names if possible
    if isinstance(x_train, pd.DataFrame):
        X_tr = x_train.values
        cols = list(x_train.columns)
    else:
        X_tr = np.asarray(x_train)
        cols = None

    if isinstance(x_test, pd.DataFrame):
        X_te = x_test.values
        if cols is None:
            cols = list(x_test.columns)
    else:
        X_te = np.asarray(x_test)

    if feature_names is None and cols is not None:
        feature_names = cols

    # Build domain-labeled dataset: 0 = source(train), 1 = target(test)
    n_tr, n_te = len(X_tr), len(X_te)
    X_adv = np.vstack([X_tr, X_te])
    y_adv = np.r_[np.zeros(n_tr, dtype=int), np.ones(n_te, dtype=int)]

    # Train/validation split on the concatenated dataset
    stratify_vec = y_adv if stratify else None
    idx = np.arange(len(X_adv))
    tr_idx, va_idx = train_test_split(
        idx,
        test_size=float(test_size),
        random_state=random_state,
        stratify=stratify_vec,
        shuffle=True,
    )

    X_tr_split, y_tr_split = X_adv[tr_idx], y_adv[tr_idx]
    X_va_split, y_va_split = X_adv[va_idx], y_adv[va_idx]

    # Prepare model
    if model is None:
        model = LogisticRegression()

    mdl = _safe_clone(model)
    _maybe_set_random_state(mdl, random_state)

    # Fit and evaluate
    mdl.fit(X_tr_split, y_tr_split)
    val_probs = _predict_proba_smart(mdl, X_va_split)
    # Threshold at 0.5 for binary preds
    val_preds = (val_probs >= 0.5).astype(int)
    score = float(metric(y_va_split, val_probs))

    out = {
        "avg_score": score,
        "val_probs": np.asarray(val_probs, dtype=float),
        "val_preds": np.asarray(val_preds, dtype=int),
        'models': [mdl]
    }

    return out

from typing import Any, Optional


def _safe_clone(estimator: Any | None) -> Any | None:
    """
    Best-effort 'safe' clone of an estimator-like object.

    - If sklearn is available, use sklearn.base.clone (resets fitted state).
    - Otherwise, if the object exposes get_params, re-instantiates the class
      with those params.
    - Returns None unchanged.
    """
    if estimator is None:
        return None

    # Prefer sklearn.clone if available (handles Pipelines, meta-estimators, etc.)
    try:
        from sklearn.base import clone as sk_clone  # type: ignore
        return sk_clone(estimator)
    except Exception:
        pass  # fall back to a simple get_params-based clone

    # Generic fallback for estimator-like objects
    if hasattr(estimator, "get_params"):
        params = estimator.get_params(deep=True)
        return estimator.__class__(**params)

    # At this point we don't know how to clone without copying learned state.
    raise TypeError(
        f"Cannot safely clone object of type {type(estimator)!r}; "
        "no sklearn.clone and no get_params() found."
    )


def _maybe_set_random_state(
    obj: Any | None,
    random_state: Optional[int] = None,
) -> Any | None:
    """
    Set random_state on estimator-like objects *if* possible.

    - If random_state is None or obj is None, it's a no-op.
    - Handles:
        * single estimators
        * lists / tuples of estimators
        * dicts of estimators
    - Tries set_params(random_state=...), then falls back to attribute assignment
      if a .random_state attribute exists.
    """
    if obj is None or random_state is None:
        return obj

    # Containers: apply recursively
    if isinstance(obj, (list, tuple)):
        for est in obj:
            _maybe_set_random_state(est, random_state)
        return obj

    if isinstance(obj, dict):
        for est in obj.values():
            _maybe_set_random_state(est, random_state)
        return obj

    # Estimator-like object
    # 1) Try sklearn-style set_params
    if hasattr(obj, "set_params"):
        try:
            obj.set_params(random_state=random_state)
            return obj
        except (TypeError, ValueError):
            # Not all estimators expose random_state in set_params
            pass

    # 2) Fall back to direct attribute if present
    if hasattr(obj, "random_state"):
        try:
            setattr(obj, "random_state", random_state)
        except Exception:
            pass

    return obj


