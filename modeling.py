def _ensure_dir(d: Optional[Union[str, Path]]) -> Optional[Path]:
    if d is None:
        return None
    p = Path(d)
    p.mkdir(parents=True, exist_ok=True)
    return p

def _save_automl_state(save_dir: Path, automl: Any, prefix: str = "flaml") -> None:
    pkl_path = save_dir / f"{prefix}_automl.pkl"
    meta_path = save_dir / f"{prefix}_best_meta.json"

    # pickle whole automl (best for inference/reuse)
    with open(pkl_path, "wb") as f:
        pickle.dump(automl, f)

    meta = {
        "best_estimator": getattr(automl, "best_estimator", None),
        "best_loss": getattr(automl, "best_loss", None),
        "best_config": getattr(automl, "best_config", None),
    }
    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(meta, f, indent=2, default=str)

def _load_automl_state(load_dir: Path, prefix: str = "flaml") -> Optional[Any]:
    pkl_path = load_dir / f"{prefix}_automl.pkl"
    if not pkl_path.exists():
        return None
    with open(pkl_path, "rb") as f:
        return pickle.load(f)

