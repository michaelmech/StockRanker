def engineer_specific_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    Computes all technical indicators, various returns, volatility metrics,
    crossovers, interaction terms, and all lagged versions of these
    ticker-specific features.

    Parameters:
    df (pd.DataFrame): Input DataFrame with a MultiIndex (ticker, date)
                       containing 'open', 'high', 'low', 'close', 'volume' columns.

    Returns:
    pd.DataFrame: The input DataFrame with all added ticker-specific features.
    """
    df = df.copy()
    # Ensure DataFrame is sorted by ticker and date for correct time-series operations
    df = df.sort_index(level=['ticker', 'date'])

    grouped = df.groupby(level=0) # Group by ticker for all ticker-specific ops

    result_dfs = []

    for ticker, group in grouped:
        group_df = group.reset_index(level=0, drop=True)
        original_len = len(group_df)

        # --- Technical Indicators ---
        group_df['ma4'] = group_df['close'].rolling(window=4, min_periods=1).mean()
        group_df['ma8'] = group_df['close'].rolling(window=8, min_periods=1).mean()
        group_df['ma16'] = group_df['close'].rolling(window=16, min_periods=1).mean()
        group_df['ma26'] = group_df['close'].rolling(window=26, min_periods=1).mean()

        delta = group_df['close'].diff()
        gain = (delta.where(delta > 0, 0)).rolling(window=10, min_periods=1).mean()
        loss = (-delta.where(delta < 0, 0)).rolling(window=10, min_periods=1).mean()
        rs = gain / loss.replace(0, np.finfo(float).eps)
        group_df['rsi'] = 100 - (100 / (1 + rs))

        group_df['ema12'] = group_df['close'].ewm(span=12, adjust=False).mean()
        group_df['ema26'] = group_df['close'].ewm(span=26, adjust=False).mean()
        group_df['macd'] = group_df['ema12'] - group_df['ema26']
        group_df['macd_signal'] = group_df['macd'].ewm(span=9, adjust=False).mean()

        high_low = group_df['high'] - group_df['low']
        high_close = np.abs(group_df['high'] - group_df['close'].shift())
        low_close = np.abs(group_df['low'] - group_df['close'].shift())
        ranges = pd.concat([high_low, high_close, low_close], axis=1)
        true_range = np.max(ranges, axis=1)
        group_df['atr'] = true_range.rolling(window=10, min_periods=1).mean()

        group_df['bb_middle'] = group_df['close'].rolling(window=20, min_periods=1).mean()
        std_dev = group_df['close'].rolling(window=20, min_periods=1).std()
        group_df['bb_upper'] = group_df['bb_middle'] + (2 * std_dev)
        group_df['bb_lower'] = group_df['bb_middle'] - (2 * std_dev)

        group_df['volume_ma4'] = group_df['volume'].rolling(window=4, min_periods=1).mean()

        group_df['momentum_4w'] = group_df['close'] / group_df['close'].shift(4) - 1
        group_df['momentum_8w'] = group_df['close'] / group_df['close'].shift(8) - 1

        # --- Time-Series Derivatives & Basic Features ---
        group_df['pct_change_1w'] = group_df['close'].pct_change(1)
        group_df['pct_change_4w'] = group_df['close'].pct_change(4)
        group_df['weekly_return'] = group_df['close'] / group_df['open'] - 1

        group_df['log_ret'] = np.log(group_df['close'] / group_df['close'].shift())
        group_df['volatility_4w'] = group_df['log_ret'].rolling(4, min_periods=1).std()

        group_df['daily_range'] = (group_df['high'] - group_df['low']) / group_df['close']
        group_df['open_close_ret'] = (group_df['close'] - group_df['open']) / group_df['open']
        group_df['bb_position'] = (group_df['close'] - group_df['bb_lower']) / (group_df['bb_upper'] - group_df['bb_lower'])
        group_df['dist_ma26'] = (group_df['close'] - group_df['ma26']) / group_df['ma26']
        group_df['vol_rel_ma'] = group_df['volume'] / group_df['volume_ma4']

        group_df['macd_cross_signal'] = ((group_df['macd'] > group_df['macd_signal']) & \
                                         (group_df['macd'].shift() <= group_df['macd_signal'].shift())).astype(int)
        group_df['rsi_overbought'] = (group_df['rsi'] > 70).astype(int)
        group_df['rsi_oversold'] = (group_df['rsi'] < 30).astype(int)

        group_df['rsi_x_momentum'] = group_df['rsi'] * group_df['momentum_4w']
        group_df['macd_x_return'] = group_df['macd'] * group_df['weekly_return']

        # --- Ticker-Specific Deltas (derived from original fundamentals enhancement) ---
        delta_base_cols = [
            'rsi', 'macd', 'atr', 'momentum_4w', 'momentum_8w',
            'weekly_return', 'volatility_4w', 'daily_range', 'open_close_ret',
            'bb_position', 'dist_ma26', 'vol_rel_ma'
        ]
        for col in delta_base_cols:
            if col in group_df.columns:
                group_df[f"{col}_delta_fund"] = group_df[col].diff()
            else:
                pass # Already handled by print in debug, now silent for consolidated func

        # --- Lagged Features ---
        features_to_lag = [
            'open', 'high', 'low', 'close', 'volume', 'ma4', 'ma8', 'ma16', 'ma26', 'rsi',
            'weekly_return', 'macd', 'atr', 'pct_change_1w', 'pct_change_4w', 'log_ret',
            'volatility_4w', 'daily_range', 'open_close_ret', 'bb_position',
            'dist_ma26', 'vol_rel_ma', 'macd_cross_signal', 'rsi_overbought', 'rsi_oversold',
            'rsi_x_momentum', 'macd_x_return'
        ] + [f"{col}_delta_fund" for col in delta_base_cols if f"{col}_delta_fund" in group_df.columns]

        lags = [1, 2, 4, 8, 20, 40]
        for feature in features_to_lag:
            if feature in group_df.columns:
                for lag in lags:
                    group_df[f'{feature}_lag{lag}'] = group_df[feature].shift(lag)

        # Verify length
        if len(group_df) != original_len:
            raise ValueError(f"Length mismatch for ticker {ticker}: expected {original_len}, got {len(group_df)}")

        group_df = group_df.set_index(
            pd.Series([ticker] * len(group_df.index), name='ticker'), append=True
        )
        result_dfs.append(group_df)

    result = pd.concat(result_dfs)
    result = result.reorder_levels(['ticker', 'date'])
    return result

@check_format
def engineer_market_relative_features(
    df: pd.DataFrame,
    features_to_standardize: list[str],
    base_cols_for_rank_derivatives: list[str],
    *,
    suffix: str = "_fund",
    rolling_rank_window: int = 4,
    add_rank_shock: bool = True,
    add_rank_trend: bool = True,
    long_lags: tuple[int, ...] = (1, 3, 5, 10, 20),
) -> pd.DataFrame:
    """
    Vectorised re-implementation of engineer_market_relative_features.
    Produces identical columns; ~3-10× faster on typical equity universes.
    """

    out = df.copy()
    out = out.sort_index(level=['date', 'ticker'])

    # -------- 1. Cross-sectional ops (batch) --------
    feats = [c for c in features_to_standardize if c in out.columns]
    by_date = out.groupby(level='date')

    z_df = by_date[feats].transform(lambda x: (x - x.mean()) / (x.std(ddof=0) + 1e-8))
    rk_df = by_date[feats].rank(pct=True)

    z_df.columns  = [f"z_{c}{suffix}" for c in feats]
    rk_df.columns = [f"{c}_rank_pct{suffix}" for c in feats]

    out = pd.concat([out, z_df.astype("float32"), rk_df.astype("float32")], axis=1)

    # -------- 2. Derived rank features (batch) --------
    rank_cols = [f"{c}_rank_pct{suffix}" for c in base_cols_for_rank_derivatives
                 if f"{c}_rank_pct{suffix}" in out.columns]

    by_ticker = out.groupby(level=0)

    if add_rank_trend and rolling_rank_window > 0 and rank_cols:
        trend = (
            by_ticker[rank_cols]
            .rolling(rolling_rank_window, min_periods=1)
            .mean()
            .reset_index(level=0, drop=True)
        )
        trend.columns = [f"{c.replace('_rank_pct', '_rank_trend')}{suffix}" for c in base_cols_for_rank_derivatives
                         if f"{c}_rank_pct{suffix}" in out.columns]
        out = pd.concat([out, trend.astype("float32")], axis=1)

    if add_rank_shock and rank_cols:
        shock = by_ticker[rank_cols].diff().rename(
            columns={c: c.replace("_rank_pct", "_rank_shock") for c in rank_cols}
        )
        out = pd.concat([out, shock.astype("float32")], axis=1)

        # -------- 3. Lagged Z-scores (batch) --------
    z_cols = [c for c in out.columns if c.startswith("z_") and c.endswith(suffix)]
    if z_cols and long_lags:
        # Build a dict of Series
        lagged = {
            f"{col}_lag{lag}": by_ticker[col].shift(lag).astype("float32")
            for lag in long_lags
            for col in z_cols
        }

        # <<< fix starts here >>>
        lagged_df = pd.DataFrame(lagged)           # index aligns with `out`
        out = out.join(lagged_df)
        # <<< fix ends here >>>

    return out

