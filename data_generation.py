def generate_stock_data(
    tickers,
    start_date,
    end_date,
    freq: str = "1d",
    auto_adjust: bool = False,
    threads: bool = True,
):
    """
    Download OHLCV via yfinance at `freq` (yfinance interval strings),
    with optional pandas resampling for 'nD' (e.g., '10d').

    Returns
    -------
    pd.DataFrame
        MultiIndex (ticker, date) with columns: open, high, low, close, volume.
    """
    if isinstance(tickers, str):
        tickers = [tickers]

    yf_interval, pandas_rule = _parse_freq(freq)

    raw = yf.download(
        tickers,
        start=start_date,
        end=end_date,
        interval=yf_interval,
        group_by="ticker",
        auto_adjust=auto_adjust,
        progress=False,
        threads=threads,
    )

    if raw.empty:
        raise ValueError("No data returned from yfinance for given tickers/date range")

    frames = []

    # Case 1: MultiIndex columns
    if isinstance(raw.columns, pd.MultiIndex):
        lvl0 = raw.columns.get_level_values(0)
        lvl1 = raw.columns.get_level_values(1)

        tickers_set = set(tickers)
        if tickers_set.issubset(set(lvl0)):
            ticker_level = 0
        elif tickers_set.issubset(set(lvl1)):
            ticker_level = 1
        else:
            raise ValueError("Could not locate tickers in returned MultiIndex columns.")

        for tkr in tickers:
            if ticker_level == 0:
                df_tkr = raw[tkr].copy()
            else:
                df_tkr = raw.xs(tkr, axis=1, level=ticker_level).copy()

            df_tkr = (
                df_tkr.rename(columns=str.lower)
                      .rename_axis("date")
                      .sort_index()
            )

            ohlcv = pd.DataFrame({
                "open":   df_tkr.get("open"),
                "high":   df_tkr.get("high"),
                "low":    df_tkr.get("low"),
                "close":  df_tkr.get("close"),
                "volume": df_tkr.get("volume"),
            }).dropna(how="all")

            # Resample if requested (only for nD / custom)
            if pandas_rule is not None:
                ohlcv = _ohlcv_resample(ohlcv, pandas_rule)

            ohlcv["ticker"] = tkr
            frames.append(ohlcv.reset_index())

    # Case 2: Single-index columns (single ticker)
    else:
        if len(tickers) != 1:
            raise ValueError("Non-MultiIndex columns returned but multiple tickers requested.")

        tkr = tickers[0]
        df_tkr = (
            raw.rename(columns=str.lower)
               .rename_axis("date")
               .sort_index()
        )

        ohlcv = pd.DataFrame({
            "open":   df_tkr.get("open"),
            "high":   df_tkr.get("high"),
            "low":    df_tkr.get("low"),
            "close":  df_tkr.get("close"),
            "volume": df_tkr.get("volume"),
        }).dropna(how="all")

        if pandas_rule is not None:
            ohlcv = _ohlcv_resample(ohlcv, pandas_rule)

        ohlcv["ticker"] = tkr
        frames.append(ohlcv.reset_index())

    all_data = (
        pd.concat(frames, ignore_index=True)
          .set_index(["ticker", "date"])
          .sort_index()
    )

    if all_data.index.duplicated().any():
        raise ValueError("Duplicate (ticker, date) entries found in fetched data")

    return all_data

