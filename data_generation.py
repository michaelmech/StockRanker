import vectorbt as vbt
def calculate_profit_vectorbt(
    data: pd.DataFrame,
    horizon: int = 1,
    *,
    stop_pct: float = np.inf,
    take_profit_pct: float = np.inf,
    entry_price_col: str = "close",
    use_ohlc: bool = True,
    chunk_size: int = 512,

    # --- new label options ---
    higher_is_better: bool = True,       # True: higher profit => higher percentile
    pct_method: str = "average",         # rank tie handling: 'average', 'min', 'max', 'dense', 'first'
    output: str = "pct",                 # "pct" -> [0,1], "signed" -> [-1,1]
) -> pd.Series:
    """
    1) Computes per-(ticker,date) realized probe profit using fixed SL/TP + time exit at horizon
       (same semantics as your existing function).
    2) Converts those profits into cross-sectional percentile ranks PER DATE.

    output:
      - "pct": percentile in [0,1]
      - "signed": maps pct to [-1,1] via (2*pct - 1)

    Notes:
      - NaNs remain NaN and are excluded from daily ranking.
      - If a given date has <2 valid names, percentile ranks may be NaN or degenerate.
    """

    if horizon < 1:
        raise ValueError("horizon must be >= 1")
    if not isinstance(data.index, pd.MultiIndex):
        raise ValueError("data must have MultiIndex with ticker as level 0 (and date as another level).")
    for col in ["close", entry_price_col]:
        if col not in data.columns:
            raise KeyError(f"Missing required column: '{col}'")

    need_ohlc = use_ohlc and all(c in data.columns for c in ["open", "high", "low"])
    if use_ohlc and not need_ohlc:
        raise KeyError("use_ohlc=True requires columns: open, high, low (in addition to close).")

    # Identify date level (keep your original intent)
    if "date" in data.index.names:
        date_level = data.index.names.index("date")
    else:
        date_level = 1 if data.index.nlevels >= 2 else None
    if date_level is None:
        raise ValueError("Could not infer date level in MultiIndex.")

    # --- Step 1: compute profits exactly like before ---
    out_profit = pd.Series(np.nan, index=data.index, dtype="float64", name="profit")

    for tkr, g in data.groupby(level=0, sort=False):
        lvl = g.index.get_level_values(date_level)
        dt = pd.to_datetime(lvl, errors="coerce")
        order = np.argsort(np.asarray(dt, dtype="datetime64[ns]"), kind="mergesort")
        g = g.iloc[order]

        n = len(g)
        if n <= horizon:
            continue

        close = pd.to_numeric(g["close"], errors="coerce").to_numpy(float)
        entry_px = pd.to_numeric(g[entry_price_col], errors="coerce").to_numpy(float)

        if need_ohlc:
            opn  = pd.to_numeric(g["open"], errors="coerce").to_numpy(float)
            high = pd.to_numeric(g["high"], errors="coerce").to_numpy(float)
            low  = pd.to_numeric(g["low"], errors="coerce").to_numpy(float)
        else:
            opn = high = low = None

        valid_entry = np.isfinite(entry_px) & (entry_px > 0)
        valid_i = np.where(valid_entry)[0]
        valid_i = valid_i[valid_i <= (n - 1 - horizon)]
        if valid_i.size == 0:
            continue

        profits_sorted = np.full(n, np.nan, dtype=float)

        for start in range(0, valid_i.size, chunk_size):
            idx_chunk = valid_i[start:start + chunk_size]
            m = idx_chunk.size

            close_df = pd.DataFrame(
                np.tile(close.reshape(-1, 1), (1, m)),
                index=np.arange(n),
                columns=np.arange(m)
            )

            entries = np.zeros((n, m), dtype=bool)
            entries[idx_chunk, np.arange(m)] = True

            exits = np.zeros((n, m), dtype=bool)
            exits[idx_chunk + horizon, np.arange(m)] = True

            pf_kwargs = dict(
                close=close_df,
                entries=pd.DataFrame(entries, index=close_df.index, columns=close_df.columns),
                exits=pd.DataFrame(exits, index=close_df.index, columns=close_df.columns),
                init_cash=1.0,
                size=1.0,
                size_type="value",
                fees=0.0,
                slippage=0.0,
                sl_stop=float(stop_pct),
                sl_trail=False,
                tp_stop=float(take_profit_pct),
            )

            if need_ohlc:
                open_df = pd.DataFrame(np.tile(opn.reshape(-1, 1), (1, m)), index=close_df.index, columns=close_df.columns)
                high_df = pd.DataFrame(np.tile(high.reshape(-1, 1), (1, m)), index=close_df.index, columns=close_df.columns)
                low_df  = pd.DataFrame(np.tile(low.reshape(-1, 1), (1, m)), index=close_df.index, columns=close_df.columns)
                pf_kwargs.update(open=open_df, high=high_df, low=low_df)

            pf = vbt.Portfolio.from_signals(**pf_kwargs)
            rets = pf.total_return().to_numpy(dtype=float)
            profits_sorted[idx_chunk] = rets

        out_profit.loc[g.index] = profits_sorted

    out_profit.name = "profit"

    # --- Step 2: convert profit -> cross-sectional percentile per date ---
    # Higher profit should map to higher percentile.
    # For pandas rank: ascending=True gives smallest rank=1, largest rank=N; pct=True makes largest -> 1.0
    ascending = True if higher_is_better else False

    out_pct = out_profit.groupby(level=date_level).rank(
        method=pct_method,
        pct=True,
        ascending=ascending
    )

    if output == "pct":
        out_pct.name = "y_rank_pct"
        return out_pct

    if output == "signed":
        out_signed = 2.0 * out_pct - 1.0
        out_signed.name = "y_rank_signed"
        return out_signed

    raise ValueError("output must be one of: {'pct', 'signed'}")

