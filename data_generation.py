def convert_df_index(df):
    """
    Converts the second level of a MultiIndex to string-formatted dates (YYYY-MM-DD).

    Parameters:
    - df: pandas DataFrame with a MultiIndex, where the second level is datetime-like.

    Returns:
    - A new DataFrame with the second index level converted to strings.
    """
    if not isinstance(df.index, pd.MultiIndex):
        raise ValueError("DataFrame does not have a MultiIndex.")

    # Convert second level of index
    new_index = df.index.set_levels(
        df.index.levels[1].strftime("%Y-%m-%d"),
        level=1
    )
    df.index = new_index
    return df


def check_format(func):
    """
    A decorator to ensure input and output DataFrames have a specific
    MultiIndex format with 'ticker' and 'date' as index levels.

    The decorator handles:
    1. Converting 'date' column/index level to string type in the input.
    2. Ensuring the input DataFrame to the wrapped function has a
       MultiIndex of ['ticker', 'date'], setting it if necessary from columns.
    3. Ensuring the returned DataFrame from the wrapped function has a
       MultiIndex of ['ticker', 'date'], setting it if necessary from columns
       or returning it directly if already correctly indexed.
    """
    @wraps(func)
    def wrapper(X: pd.DataFrame, *args, **kwargs):
        # Create a copy to avoid modifying the original DataFrame in place
        X_copy = X.copy()

        # --- Part 1: Prepare the input DataFrame (X_copy) for the wrapped function ---

        # Handle 'date' conversion for input: ensure 'date' is string type
        if 'date' in X_copy.columns:
            X_copy['date'] = X_copy['date'].astype(str)
        elif 'date' in X_copy.index.names:
            # Get the index of the 'date' level
            date_level_idx = X_copy.index.names.index('date')
            # Get the current levels of the 'date' index
            current_date_levels = X_copy.index.levels[date_level_idx]
            # Convert them to string type
            new_date_levels = current_date_levels.astype(str)
            # Only update if the levels have actually changed to avoid unnecessary operations
            if not current_date_levels.equals(new_date_levels):
                X_copy.index = X_copy.index.set_levels(new_date_levels, level='date')

        # Check if X_copy already has the desired MultiIndex ('ticker', 'date')
        is_input_already_multiindex = isinstance(X_copy.index, pd.MultiIndex) and X_copy.index.names == ['ticker', 'date']

        # If the input is not already correctly multi-indexed, try to set it
        if not is_input_already_multiindex:
            if 'ticker' in X_copy.columns and 'date' in X_copy.columns:
                # If 'ticker' and 'date' are columns, set them as the MultiIndex
                X_copy.set_index(['ticker', 'date'], inplace=True)
            else:
                # If the required columns/index levels are not present,
                # the input cannot be formatted as expected by the wrapped function.
                raise ValueError(
                    "Input DataFrame must have 'ticker' and 'date' as columns "
                    "or as a MultiIndex with names ['ticker', 'date']."
                )

        # --- Part 2: Call the original function with the prepared DataFrame ---
        result_df = func(X_copy, *args, **kwargs)

        # --- Part 3: Ensure the returned DataFrame (result_df) is correctly formatted ---

        # First, ensure the returned object is indeed a Pandas DataFrame
        if not isinstance(result_df, pd.DataFrame):
            raise TypeError(
                f"Wrapped function did not return a Pandas DataFrame. "
                f"Instead, received type: {type(result_df)}"
            )

        # Check if the result_df already has the correct MultiIndex ('ticker', 'date')
        is_output_already_multiindex = isinstance(result_df.index, pd.MultiIndex) and result_df.index.names == ['ticker', 'date']

        if is_output_already_multiindex:
            # If it's already in the desired MultiIndex format, return it directly.
            return result_df
        else:
            # If not, assume 'ticker' and 'date' should be columns that need
            # to be set as the MultiIndex.
            if 'ticker' not in result_df.columns or 'date' not in result_df.columns:
                # If 'ticker' or 'date' are neither in the index nor in the columns,
                # then the output DataFrame is not in a format that can be handled.
                raise ValueError(
                    "Returned DataFrame must contain 'ticker' and 'date' "
                    "as columns (if not already MultiIndexed) to set MultiIndex."
                )
            # If 'ticker' and 'date' are found as columns, set the MultiIndex
            return result_df.set_index(['ticker', 'date'])

    return wrapper

