# --- helpers ---------------------------------------------------------------

_YF_SUPPORTED_INTERVALS = {"1m","2m","5m","15m","30m","60m","90m","1h",
                           "1d","5d","1wk","1mo","3mo"}

def _ohlcv_resample(df: pd.DataFrame, rule: str) -> pd.DataFrame:
    """
    Resample per-ticker OHLCV to a new frequency rule.
    Assumes index is DatetimeIndex, columns: open/high/low/close/volume (lowercase).
    """
    agg = {
        "open": "first",
        "high": "max",
        "low": "min",
        "close": "last",
        "volume": "sum",
    }
    out = df.resample(rule).agg(agg)
    return out.dropna(subset=["open","high","low","close"], how="any")

def _parse_freq(freq: str) -> tuple[str, str | None]:
    """
    Returns (yf_interval, pandas_resample_rule or None).
    If pandas_resample_rule is not None, we will download '1d' and resample.
    """
    if not isinstance(freq, str) or not freq:
        raise ValueError("freq must be a non-empty string (e.g., '1d', '1wk', '10d').")

    f = freq.strip().lower()

    # If yfinance can do it directly, prefer that.
    if f in _YF_SUPPORTED_INTERVALS:
        return f, None

    # Allow "n days" like "10d", "21d" by resampling daily bars
    m = re.fullmatch(r"(\d+)d", f)
    if m:
        n = int(m.group(1))
        if n <= 0:
            raise ValueError("freq like 'nD' must have n >= 1.")
        # yfinance supports 1d and 5d; everything else -> resample from 1d
        if f in _YF_SUPPORTED_INTERVALS:
            return f, None
        return "1d", f"{n}D"

    # Common aliases
    if f in {"1w", "w", "week", "weekly"}:
        return "1wk", None
    if f in {"1day", "day", "daily"}:
        return "1d", None

    raise ValueError(
        f"Unsupported freq='{freq}'. Use a yfinance interval like {sorted(_YF_SUPPORTED_INTERVALS)} "
        "or an 'nD' string like '10d' (will resample from daily)."
    )

