@check_format
def enhance_fundamental_neutral(
    df: pd.DataFrame,
    base_cols: list[str],
    *,
    delta: bool = True,
    rolling_rank_window: int = 4,
    add_rank_shock: bool = True,
    add_crosssec_z: bool = True,
    suffix: str = ""
) -> pd.DataFrame:
    """
    Add market-neutral feature derivatives to `base_cols`.

    Parameters
    ----------
    df : DataFrame
        Must have a MultiIndex (ticker, date) **OR** flat cols named 'ticker'/'date'.
    base_cols : list[str]
        Columns to transform.
    delta : bool
        If True, create *_delta and *_delta_rel (cross-sectional z-score of delta).
    rolling_rank_window : int
        Window length (per ticker) for the rolling mean of daily rank-percentile.
        Set to None or 0 to skip rolling-rank features.
    add_rank_shock : bool
        If True, add *_rank_shock (= today’s rank_pct − yesterday’s).
    add_crosssec_z : bool
        If True, add a one-shot cross-sectional z-score of every `base_col`.
    suffix : str
        Optional suffix to tag the new cols (useful if you call the function twice).

    Returns
    -------
    DataFrame  (same index as input)  with the new columns appended.
    """
    out = df.copy()

    # ----- Δ and cross-sectional Δ-z -----
    if delta:
        grp_ticker = out.groupby(level=0)
        grp_date   = out.groupby(level='date')
        for col in base_cols:
            dcol = f"{col}_delta{suffix}"
            zcol = f"{col}_delta_rel{suffix}"
            out[dcol] = grp_ticker[col].diff()
            out[zcol] = grp_date[dcol].transform(
                lambda x: (x - x.mean()) / (x.std(ddof=0) + 1e-8)
            )

    # ----- Rank pct and rolling rank trend -----
    if rolling_rank_window and rolling_rank_window > 0:
        for col in base_cols:
            rcol = f"{col}_rank{suffix}"
            tcol = f"{col}_rank_trend{suffix}"
            # cross-sectional rank every date
            out[rcol] = out.groupby(level='date')[col].rank(pct=True)
            # rolling mean of that rank within ticker
            out[tcol] = out.groupby(level=0)[rcol].transform(
                lambda x: x.rolling(rolling_rank_window).mean()
            )

    # ----- Rank shocks (day-to-day change in rank pct) -----
    if add_rank_shock:
        for col in base_cols:
            rcol = f"{col}_rank{suffix}"
            scol = f"{col}_rank_shock{suffix}"
            # need the rank column from step above; compute if it doesn't exist
            if rcol not in out.columns:
                out[rcol] = out.groupby(level='date')[col].rank(pct=True)
            out[scol] = out.groupby(level=0)[rcol].diff()

    # ----- One-shot cross-sectional z for the raw levels -----
    if add_crosssec_z:
        xs_z = out.groupby(level='date')[base_cols].transform(
            lambda x: (x - x.mean()) / (x.std(ddof=0) + 1e-8)
        )
        xs_z.columns = [f"{c}_rel_z{suffix}" for c in xs_z.columns]
        out = pd.concat([out, xs_z], axis=1)

    return out

def align_quarterly_to_weekly(
    weekly_df: pd.DataFrame,
    quarterly_df: pd.DataFrame,
    suffix: str = "_q",
):
    """
    Attach the most-recent (≤) quarterly snapshot to every (ticker, week-date)
    row, with optional ffill for weeks that precede the first quarter.

    Both inputs must have a MultiIndex (ticker, date) *or* ordinary columns
    named 'ticker' and 'date'.
    """

    def _prep(df):
        # Accept either MultiIndex or flat columns
        if isinstance(df.index, pd.MultiIndex):
            df = df.reset_index()

        # Robust renaming in case levels are unnamed (0, 1)
        df = df.rename(columns={df.columns[0]: "ticker", df.columns[1]: "date"})

        # Parse dates once, coercing errors to NaT (they will drop out later)
        df["date"] = pd.to_datetime(df["date"], errors="coerce")

        # Keep only rows with a real date, then sort *by date first*
        df = df.dropna(subset=["date"]).sort_values(["date", "ticker"], ignore_index=True)
        return df

    wk = _prep(weekly_df.copy())
    qt = _prep(quarterly_df.copy())

    merged = pd.merge_asof(
        wk,
        qt,
        by="ticker",
        on="date",
        direction="backward",
        suffixes=("", suffix),
    )

    # Forward-fill quarterly columns within each ticker so that
    # weeks before the first quarterly print stay NaN (remove next 3 lines
    # if you prefer to keep those NaNs instead of back-filling).
    qcols = [c for c in merged.columns if c.endswith(suffix)]
    merged[qcols] = merged.groupby("ticker", sort=False)[qcols].ffill()

    return (
        merged.set_index(["ticker", "date"])
              .sort_index()               # nice, clean MultiIndex again
    )


