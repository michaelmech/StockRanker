def clean_and_convert_numeric_columns(df):
    """
    Identifies columns that should be numeric but might contain string representations
    of numbers or 'None'/'null' and converts them to numeric, coercing errors to NaN.

    Args:
        df (pd.DataFrame): The input DataFrame.

    Returns:
        pd.DataFrame: The DataFrame with relevant columns converted to numeric.
    """
    df_cleaned = df.copy() # Work on a copy to avoid modifying the original directly

    for col in df_cleaned.columns:
        # Try converting the column to numeric.
        # 'errors='coerce' will turn any non-numeric values (like 'None', 'null',
        # or other non-convertible strings) into NaN.
        converted_col = pd.to_numeric(df_cleaned[col], errors='coerce')

        # Check if the conversion actually resulted in a numeric type
        # AND if it introduced any NaNs that weren't there before (indicating
        # that non-numeric strings were present) or if it successfully converted
        # a string/object column to numeric.

        # A simple heuristic: if the column's dtype changed from object to a numeric type,
        # or if the non-null values are now numeric and it was an object type.

        # More precise check: If the original column's dtype was 'object' (string mix)
        # AND the converted column's dtype is now numeric (int, float)
        # OR if it's already numeric but has 'None' as a string, to ensure it handles it.

        # Let's be more direct: if the original column contains strings/objects
        # and after conversion it *can* be numeric, then replace.

        # We replace if the new column is numeric OR if it contains NaNs but
        # the *original* was not fully numeric (i.e., it had non-numeric strings).
        # This prevents converting genuinely string columns with some numbers (like IDs)
        # into floats if that's not desired.

        # A robust way is to check if it improved the dtype towards numeric.
        # If the original column was 'object' (strings, mixed types)
        # and the coerced column is now a number (float, int), then we replace.
        if df_cleaned[col].dtype == 'object' or df_cleaned[col].apply(lambda x: isinstance(x, (str, type(None)))).any():
            if pd.api.types.is_numeric_dtype(converted_col):
                # Ensure we don't accidentally convert columns that are legitimately strings
                # but might contain numbers (like '2023-12-31' which is a date string).
                # This is why 'errors=coerce' is powerful.
                df_cleaned[col] = converted_col
            elif pd.api.types.is_object_dtype(converted_col) and (converted_col.isna().sum() > 0 or df_cleaned[col].apply(lambda x: isinstance(x, (str, type(None))) and str(x).lower() in ['none', 'null']).any()):
                 # This condition handles cases where 'to_numeric' might not fully convert
                 # but we still want to clean 'None' strings to NaN
                 df_cleaned[col] = converted_col # Assign the coerced column, letting NaNs be in place.

    return df_cleaned

