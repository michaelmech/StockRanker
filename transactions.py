def advprob_to_pcts(
    adv_prob: float,
    base_stop: float = 0.08,
    min_stop: float = 0.02,
    base_tp: float = 0.15,
    min_tp: float = 0.04,
) -> tuple[float, float]:
    """Map adv_prob in [0,1] to stop/tp percents (decimals)."""
    adv = float(min(max(adv_prob, 0.0), 1.0))
    conf = 1.0 - adv
    stop_pct = min_stop + conf * (base_stop - min_stop)
    tp_pct = min_tp + conf * (base_tp - min_tp)
    return float(stop_pct), float(tp_pct)


