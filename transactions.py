def submit_entry_bracket_market(
    trading_client: TradingClient,
    symbol: str,
    qty: int,
    side: OrderSide,
    adv_prob: float,
    base_stop: float = 0.08,
    min_stop: float = 0.02,
    base_tp: float = 0.15,
    min_tp: float = 0.04,
    max_retries: int = 1,
):
    """Submit a MARKET BRACKET order (entry + attached TP/SL).

    Uses live quote for pricing, and if Alpaca returns a base_price constraint error,
    adjusts TP/SL against the returned base_price and retries once.
    """
    if qty <= 0:
        return None

    stop_pct, tp_pct = advprob_to_pcts(
        adv_prob,
        base_stop=base_stop,
        min_stop=min_stop,
        base_tp=base_tp,
        min_tp=min_tp,
    )

    def build_order(ref_price: float) -> tuple[MarketOrderRequest, float, float]:
        stop_price, tp_price = pct_to_bracket_prices(ref_price, side, stop_pct, tp_pct)
        stop_price, tp_price = apply_alpaca_bracket_guardrails(ref_price, side, stop_price, tp_price)
        o = MarketOrderRequest(
            symbol=symbol,
            qty=qty,
            side=side,
            time_in_force=TimeInForce.DAY,
            order_class=OrderClass.BRACKET,
            take_profit={"limit_price": tp_price},
            stop_loss={"stop_price": stop_price},
        )
        return o, stop_price, tp_price

    # First attempt uses the latest quote midpoint
    ref_price = get_live_mid_price(symbol)
    order, stop_price, tp_price = build_order(ref_price)

    attempt = 0
    while True:
        try:
            placed = trading_client.submit_order(order)
            print(
                f"{symbol}: BRACKET {side.value} {qty} | ref={ref_price:.2f} "
                f"TP={tp_price} SL={stop_price} id={placed.id}"
            )
            return placed
        except Exception as e:
            # Try to recover from Alpaca's base_price +/- 0.01 constraint errors.
            # The APIError message is usually a JSON string with base_price.
            err_text = str(e)

            if attempt >= max_retries:
                print(f"Error submitting bracket for {symbol}: {err_text}")
                return None

            base_price = None
            try:
                import json

                payload = json.loads(err_text) if err_text.strip().startswith("{") else None
                if isinstance(payload, dict) and "base_price" in payload:
                    base_price = float(payload["base_price"])
            except Exception:
                base_price = None

            if base_price is None:
                # Not the base_price error shape; don't retry.
                print(f"Error submitting bracket for {symbol}: {err_text}")
                return None

            # Rebuild TP/SL against the returned base_price
            # BUY:  TP >= base + 0.01, SL <= base - 0.01
            # SELL: TP <= base - 0.01, SL >= base + 0.01
            ref_price = base_price
            order, stop_price, tp_price = build_order(ref_price)

            # Extra hard constraints (in case rounding still violates)
            if side == OrderSide.BUY:
                tp_price = max(tp_price, round(base_price + 0.01, 2))
                stop_price = min(stop_price, round(base_price - 0.01, 2))
            else:
                tp_price = min(tp_price, round(base_price - 0.01, 2))
                stop_price = max(stop_price, round(base_price + 0.01, 2))

            order = MarketOrderRequest(
                symbol=symbol,
                qty=qty,
                side=side,
                time_in_force=TimeInForce.DAY,
                order_class=OrderClass.BRACKET,
                take_profit={"limit_price": tp_price},
                stop_loss={"stop_price": stop_price},
            )

            attempt += 1
            print(
                f"{symbol}: retrying with base_price={base_price:.3f} "
                f"TP={tp_price} SL={stop_price}"
            )


def submit_plain_market(
    trading_client: TradingClient,
    symbol: str,
    qty: int,
    side: OrderSide,
):
    """Submit a plain MARKET order (used for reducing/closing exposure)."""
    if qty <= 0:
        return None

    order = OrderRequest(
        symbol=symbol,
        qty=qty,
        side=side,
        type=OrderType.MARKET,
        time_in_force=TimeInForce.DAY,
    )

    placed = trading_client.submit_order(order)
    print(f"{symbol}: MARKET {side.value} {qty} id={placed.id}")
    return placed


