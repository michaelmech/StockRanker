def pct_to_bracket_prices(entry_price: float, side: OrderSide, stop_pct: float, tp_pct: float) -> tuple[float, float]:
    """Convert stop/tp percents into absolute prices."""
    if entry_price <= 0:
        raise ValueError("entry_price must be > 0")

    if side == OrderSide.BUY:
        stop_price = entry_price * (1.0 - stop_pct)
        tp_price = entry_price * (1.0 + tp_pct)
    else:  # SELL (short entry)
        stop_price = entry_price * (1.0 + stop_pct)
        tp_price = entry_price * (1.0 - tp_pct)

    return round(stop_price, 2), round(tp_price, 2)


def apply_alpaca_bracket_guardrails(ref_price: float, side: OrderSide, stop_price: float, tp_price: float) -> tuple[float, float]:
    """Ensure bracket legs are on correct side of ref price by at least $0.01."""
    if ref_price <= 0:
        raise ValueError("ref_price must be > 0")

    # Take-profit constraints
    if side == OrderSide.BUY:
        if tp_price < ref_price + 0.01:
            tp_price = round(ref_price + 0.01, 2)
        # Stop should be below ref for longs (if rounding pushed it above, nudge)
        if stop_price >= ref_price:
            stop_price = round(ref_price - 0.01, 2)
    else:
        if tp_price > ref_price - 0.01:
            tp_price = round(ref_price - 0.01, 2)
        # Stop should be above ref for shorts
        if stop_price <= ref_price:
            stop_price = round(ref_price + 0.01, 2)

    return stop_price, tp_price

