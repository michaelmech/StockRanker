def rebalance_with_live_brackets(
    trading_client: TradingClient,
    df: pd.DataFrame,
    close_others: bool = True,
):
    """Rebalance to target shares. Uses BRACKET only when opening/increasing exposure."""

    required = {"symbol", "shares"}
    if not required.issubset(df.columns):
        raise ValueError("df must include columns: 'symbol', 'shares'")

    # If you want bracket sizing from adv_prob, ensure it is present
    if "adv_prob" not in df.columns:
        raise ValueError("df must include 'adv_prob' for bracket entries")

    current_positions = get_current_positions_dict(trading_client)

    target_positions = {row["symbol"]: int(row["shares"]) for _, row in df.iterrows()}

    for _, row in df.iterrows():
        symbol = str(row["symbol"]).upper()
        target = int(row["shares"])
        adv_prob = float(row["adv_prob"])

        current = current_positions.get(symbol, 0)
        delta = target - current

        if delta == 0:
            print(f"{symbol}: already at target {target}")
            continue

        side = OrderSide.BUY if delta > 0 else OrderSide.SELL
        qty = abs(int(delta))

        # Opening/increasing exposure → bracket.
        # - current==0: opening
        # - long (current>0) and delta>0: adding
        # - short (current<0) and delta<0: adding to short
        opening_or_increasing = (current == 0) or (current > 0 and delta > 0) or (current < 0 and delta < 0)

        if opening_or_increasing:
            submit_entry_bracket_market(
                trading_client=trading_client,
                symbol=symbol,
                qty=qty,
                side=side,
                adv_prob=adv_prob,
            )
        else:
            # Reducing exposure or flipping direction → plain market (simplest & safest)
            submit_plain_market(
                trading_client=trading_client,
                symbol=symbol,
                qty=qty,
                side=side,
            )

    if close_others:
        symbols_to_close = set(current_positions.keys()) - set(target_positions.keys())
        for symbol in symbols_to_close:
            current = current_positions[symbol]
            if current == 0:
                continue
            # Trade back to zero
            side = OrderSide.SELL if current > 0 else OrderSide.BUY
            qty = abs(int(current))
            print(f"{symbol}: not in targets, closing {current} shares")
            submit_plain_market(trading_client, symbol, qty, side)