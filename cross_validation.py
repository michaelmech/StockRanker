def cross_val_spearman_score(estimator, X, y, cv, n_jobs=-1):
    """
    Custom cross-validation scoring using Spearman correlation with parallel processing.

    Parameters:
    - estimator: scikit-learn compatible estimator with .fit and .predict
    - X: pd.DataFrame of features
    - y: target values (array-like)
    - cv: cross-validator (e.g., RollingPurgedKFold)
    - n_jobs: int, number of parallel jobs (-1 uses all available cores)

    Returns:
    - mean Spearman correlation across folds
    - list of per-fold Spearman correlations
    """
    # Parallelize computation across folds
    scores = Parallel(n_jobs=n_jobs)(
        delayed(compute_fold_score)(estimator, X, y, train_idx, test_idx)
        for train_idx, test_idx in cv.split(X, y)
    )

    return np.mean(scores), scores

def create_regime_feats(df_input):

    df = df_input.copy()

    if 'date' not in df.index.names and 'date' in df.index:
      df=df.set_index(['ticker','date'])

    # Convert all column names to lowercase for consistent access
    df.columns = df.columns.str.lower()

    # Sort the DataFrame by Date
    df.sort_index(inplace=True)

    # 1. Log Returns
    df['log_returns'] = df['close'].apply(lambda x: np.log(x)).diff()

    # 2. Historical Volatility (Rolling Standard Deviation of Log Returns)
    # Using a 21-day window (approx. one trading month)
    df['historical_volatility_21d'] = df['log_returns'].rolling(window=21).std() * np.sqrt(252) # Annualized

    # 3. Day's Range
    df['day_range'] = df['high'] - df['low']

    # 4. Bollinger Bands (20-day Moving Average, 2 Standard Deviations)
    window = 20
    df['bb_middle_band'] = df['close'].rolling(window=window).mean()
    df['bb_upper_band'] = df['bb_middle_band'] + (df['close'].rolling(window=window).std() * 2)
    df['bb_lower_band'] = df['bb_middle_band'] - (df['close'].rolling(window=window).std() * 2)
    df['bb_width'] = df['bb_upper_band'] - df['bb_lower_band']

    # 5. Moving Averages
    df['ma_50'] = df['close'].rolling(window=50).mean()
    df['ma_200'] = df['close'].rolling(window=200).mean()

    # 6. Skewness and Kurtosis of Log Returns
    # Using a 21-day window
    df['rolling_skewness_21d'] = df['log_returns'].rolling(window=21).skew()
    df['rolling_kurtosis_21d'] = df['log_returns'].rolling(window=21).kurt()

    # 7. Maximum Drawdown (Rolling)
    # This is a bit more complex for a rolling window, as it needs to track the peak.
    # Let's calculate the cumulative returns first to apply rolling drawdown.
    df['cumulative_returns'] = (1 + df['log_returns']).cumprod() - 1
    df['rolling_max_252d'] = df['cumulative_returns'].rolling(window=252, min_periods=1).max()
    df['rolling_drawdown_252d'] = df['cumulative_returns'] / df['rolling_max_252d'] - 1

    # List of engineered columns to return
    engineered_columns = [
        'log_returns',
        'historical_volatility_21d',
        'day_range',
        'bb_middle_band',
        'bb_upper_band',
        'bb_lower_band',
        'bb_width',
        'ma_50',
        'ma_200',
        'rolling_skewness_21d',
        'rolling_kurtosis_21d',
        'cumulative_returns',
        'rolling_max_252d',
        'rolling_drawdown_252d'
    ]

    return df[engineered_columns]

