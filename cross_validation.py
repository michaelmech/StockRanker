class RegimePurgedEmbargoCV(BaseCrossValidator):
    """
    Regime-based cross-validation with purge and embargo periods.

    Takes a list of regime definitions (regime_id, start_date, end_date) and generates
    temporally valid train/test splits where test regimes always come after train regimes.

    Parameters
    ----------
    regimes : list of tuples
        List of (regime_id, start_date_str, end_date_str) defining the regimes
    purge_period : int, default=1
        Number of **time steps** (unique dates in X) to purge before test periods start
    embargo_period : int, default=1
        Number of **time steps** to embargo at the start of test periods
    datetime_level : str, default='date'
        Name of datetime level in the pandas MultiIndex
    ticker_level : str, default='ticker'
        Name of ticker level in the pandas MultiIndex
    max_test_regimes : int, default=3
        Maximum number of regimes to use in a single test set
    max_splits : int or None
        Maximum number of splits to use
    optimize_training_data : bool, default=True
        If True, optimize split selection to maximize training data while ensuring test coverage
    random_state : int, RandomState instance or None, default=None
        Controls random selection when max_splits is specified and optimize_training_data=False
    """

    def __init__(self,
                 regimes,
                 purge_period=1,
                 embargo_period=1,
                 datetime_level='date',
                 ticker_level='ticker',
                 max_test_regimes=3,
                 max_splits=None,
                 optimize_training_data=True,
                 random_state=None):
        self.regimes = regimes
        self.purge_period = purge_period
        self.embargo_period = embargo_period
        self.datetime_level = datetime_level
        self.ticker_level = ticker_level
        self.max_test_regimes = max_test_regimes
        self.max_splits = max_splits
        self.optimize_training_data = optimize_training_data
        self.random_state = random_state

        # Process regimes on initialization
        self.processed_regimes = self._postprocess_regimes()
        self.valid_splits = self._generate_valid_splits()

    def _postprocess_regimes(self):
        """
        Remove temporal overlaps from regimes, keeping earliest start dates.
        """
        regime_data = []
        for regime_id, start_str, end_str in self.regimes:
            regime_data.append({
                'regime_id': regime_id,
                'start_date': pd.to_datetime(start_str),
                'end_date': pd.to_datetime(end_str)
            })

        regime_data.sort(key=lambda x: x['start_date'])

        processed = []
        for regime in regime_data:
            current = regime.copy()

            for prev in processed:
                if current['start_date'] <= prev['end_date']:
                    # Overlap detected - adjust current start date
                    current['start_date'] = prev['end_date'] + timedelta(days=1)

            if current['start_date'] < current['end_date']:
                processed.append(current)

        return processed

    def _generate_valid_splits(self):
        """
        Generate all temporally valid train/test combinations.
        Test regimes must come after train regimes.
        """
        valid_splits = []
        n_regimes = len(self.processed_regimes)

        for split_point in range(1, n_regimes):
            train_candidates = self.processed_regimes[:split_point]
            test_candidates = self.processed_regimes[split_point:]

            for test_size in range(1, min(self.max_test_regimes + 1,
                                          len(test_candidates) + 1)):
                for test_regimes in combinations(test_candidates, test_size):
                    train_data_size = self._calculate_train_data_size(
                        train_candidates, test_regimes
                    )
                    valid_splits.append({
                        'train_regimes': train_candidates,
                        'test_regimes': list(test_regimes),
                        'split_point': split_point,
                        'train_data_size': train_data_size
                    })

        return valid_splits

    def _calculate_train_data_size(self, train_regimes, test_regimes):
        """
        Rough estimate of training duration in days for a given split.

        This is only used for ranking splits; it still uses calendar days.
        """
        total_days = 0
        first_test_start = min(regime['start_date'] for regime in test_regimes)

        for regime in train_regimes:
            train_start = regime['start_date']
            train_end = min(
                regime['end_date'],
                first_test_start - timedelta(days=self.purge_period + 1)
            )
            if train_start <= train_end:
                total_days += (train_end - train_start).days + 1

        return total_days

    def _optimize_split_selection(self, all_splits, max_splits):
        """
        Select splits that maximize training data while ensuring comprehensive test coverage.
        """
        if len(all_splits) <= max_splits:
            return all_splits

        all_test_regimes = {r['regime_id'] for r in self.processed_regimes}
        selected_splits = []
        covered_regimes = set()

        remaining_splits = sorted(
            all_splits,
            key=lambda x: x['train_data_size'],
            reverse=True
        )

        # Phase 1: cover each regime at least once
        for regime_id in all_test_regimes:
            if len(selected_splits) >= max_splits:
                break

            best_split = None
            best_idx = -1

            for i, split in enumerate(remaining_splits):
                test_regime_ids = {r['regime_id'] for r in split['test_regimes']}
                if regime_id not in test_regime_ids:
                    continue

                new_coverage = test_regime_ids - covered_regimes
                if new_coverage:
                    best_split = split
                    best_idx = i
                    break

            if best_split is not None:
                selected_splits.append(best_split)
                covered_regimes.update({r['regime_id'] for r in best_split['test_regimes']})
                remaining_splits.pop(best_idx)

        # Phase 2: fill remaining with best training data splits
        remaining_slots = max_splits - len(selected_splits)
        if remaining_slots > 0:
            remaining_splits.sort(
                key=lambda x: (
                    x['train_data_size'],
                    -x['split_point'],
                    len(x['test_regimes'])
                ),
                reverse=True
            )
            selected_splits.extend(remaining_splits[:remaining_slots])

        return selected_splits

    def get_n_splits(self, X=None, y=None, groups=None):
        if self.max_splits is not None:
            return min(self.max_splits, len(self.valid_splits))
        return len(self.valid_splits)

    def _get_groups_array(self, X, groups):
        """
        Normalize 'time' info into a 1D array of python dates, agnostic
        to frequency (daily/weekly/etc.).

        - If X is a pandas object with a MultiIndex containing datetime_level,
          we ignore 'groups' and derive them from the index.
        - Otherwise, we require a 'groups' array with datetime-like values.
        """
        if isinstance(X, (pd.DataFrame, pd.Series)) and isinstance(X.index, pd.MultiIndex):
            if self.datetime_level not in X.index.names:
                raise ValueError(
                    f"datetime_level '{self.datetime_level}' not found in index names: {X.index.names}"
                )
            idx_dates = pd.to_datetime(X.index.get_level_values(self.datetime_level))
            return np.array([d.date() for d in idx_dates])

        if groups is None:
            raise ValueError(
                "When X is not a pandas object with a MultiIndex datetime index, "
                "you must pass 'groups' as a datetime-like array aligned with X."
            )

        groups = np.asarray(groups)
        if groups.shape[0] != len(X):
            raise ValueError(
                f"'groups' must have the same length as X; "
                f"got len(groups)={groups.shape[0]}, len(X)={len(X)}."
            )

        grp_dt = pd.to_datetime(groups)
        return np.array([d.date() for d in grp_dt])

    def split(self, X, y=None, groups=None):
        """
        Generate indices to split data into training and test sets.

        Supports:
          - pandas DataFrame/Series with MultiIndex (ticker, date-like)
          - NumPy arrays, when a datetime-like 'groups' array is provided

        Datetime frequency (daily, weekly, etc.) is handled by comparing
        date *boundaries* against these groups. Purge/embargo are in units
        of unique time steps (e.g., 1 = one bar, daily or weekly).
        """
        # 1) per-sample date array
        groups_dates = self._get_groups_array(X, groups)
        n_samples = len(groups_dates)

        # 1b) unique sorted dates = time steps
        unique_dates = np.unique(groups_dates)
        unique_dates.sort()
        unique_dates_np = np.array(unique_dates)

        # helper to map regime timestamps to indices in unique_dates
        def _left_pos(ts):
            d = ts.date()
            i = unique_dates_np.searchsorted(d, side="left")
            if i >= len(unique_dates_np):
                return None
            return i

        def _right_pos(ts):
            d = ts.date()
            j = unique_dates_np.searchsorted(d, side="right") - 1
            if j < 0:
                return None
            return j

        # 2) choose which regime splits to use
        splits_to_use = self.valid_splits
        if self.max_splits and self.max_splits < len(self.valid_splits):
            if self.optimize_training_data:
                splits_to_use = self._optimize_split_selection(
                    self.valid_splits, self.max_splits
                )
            else:
                rng = check_random_state(self.random_state)
                indices = rng.choice(
                    len(self.valid_splits),
                    size=self.max_splits,
                    replace=False
                )
                splits_to_use = [self.valid_splits[i] for i in indices]

        # 3) map regime date ranges -> masks
        for split_info in splits_to_use:
            train_regimes = split_info['train_regimes']
            test_regimes = split_info['test_regimes']

            if not train_regimes or not test_regimes:
                continue

            # earliest test start (in index units)
            first_test_start_ts = min(r['start_date'] for r in test_regimes)
            first_test_start_idx = _left_pos(first_test_start_ts)
            if first_test_start_idx is None:
                continue

            train_mask = np.zeros(n_samples, dtype=bool)
            test_mask = np.zeros(n_samples, dtype=bool)

            # ---- TRAIN MASK (purge vs first test start, frequency-aware) ----
            for regime in train_regimes:
                r_start_idx = _left_pos(regime['start_date'])
                r_end_idx = _right_pos(regime['end_date'])
                if r_start_idx is None or r_end_idx is None:
                    continue

                # training ends purge_period steps before first test start
                max_train_end_idx = first_test_start_idx - (self.purge_period + 1)
                train_end_idx = min(r_end_idx, max_train_end_idx)

                if r_start_idx <= train_end_idx:
                    start_d = unique_dates[r_start_idx]
                    end_d = unique_dates[train_end_idx]
                    train_mask |= (
                        (groups_dates >= start_d) &
                        (groups_dates <= end_d)
                    )

            # ---- TEST MASK (embargo/purge around regime bounds, frequency-aware) ----
            for regime in test_regimes:
                r_start_idx = _left_pos(regime['start_date'])
                r_end_idx = _right_pos(regime['end_date'])
                if r_start_idx is None or r_end_idx is None:
                    continue

                # embargo shifts start forward by embargo_period steps
                test_start_idx = r_start_idx + self.embargo_period
                # purge shifts end backward by purge_period steps
                test_end_idx = r_end_idx - self.purge_period

                if test_start_idx <= test_end_idx:
                    start_d = unique_dates[test_start_idx]
                    end_d = unique_dates[test_end_idx]
                    test_mask |= (
                        (groups_dates >= start_d) &
                        (groups_dates <= end_d)
                    )

            if not train_mask.any() or not test_mask.any():
                continue

            train_idx = np.where(train_mask)[0]
            test_idx = np.where(test_mask)[0]

            if len(train_idx) == 0 or len(test_idx) == 0:
                continue

            yield train_idx, test_idx

from dataclasses import dataclass
from datetime import timedelta
from typing import Iterable, List, Tuple, Dict, Optional, Any, Sequence

import numpy as np
import pandas as pd
from sklearn.model_selection import BaseCrossValidator

@dataclass(frozen=True)
class RegimeDefinition:
    """Container for a single regime.

    Parameters
    ----------
    regime_id : Any
        Identifier for the regime (int, str, etc.).
    start_date : pd.Timestamp
        Inclusive start date of the regime.
    end_date : pd.Timestamp
        Inclusive end date of the regime.
    """

    regime_id: Any
    start_date: pd.Timestamp
    end_date: pd.Timestamp

    @classmethod
    def from_any(cls, raw: Any) -> "RegimeDefinition":
        """Build from a tuple or dict.

        Accepts:
        - (regime_id, start, end)
        - {"regime_id": ..., "start_date": ..., "end_date": ...}
        where dates are anything `pd.to_datetime` can parse.
        """

        if isinstance(raw, dict):
            rid = raw.get("regime_id")
            start = raw.get("start_date")
            end = raw.get("end_date")
        else:
            if len(raw) != 3:
                raise ValueError(
                    "Regime tuple must be (regime_id, start_date, end_date). "
                    f"Got: {raw!r}"
                )
            rid, start, end = raw

        start_ts = pd.to_datetime(start)
        end_ts = pd.to_datetime(end)

        if pd.isna(start_ts) or pd.isna(end_ts):
            raise ValueError(f"Invalid start/end date in regime {raw!r}")
        if start_ts > end_ts:
            raise ValueError(
                f"Regime start_date must be <= end_date. Got {start_ts} > {end_ts}"
            )

        return cls(regime_id=rid, start_date=start_ts, end_date=end_ts)


class BalancedRegimePurgedEmbargoCV(BaseCrossValidator):
    """Regime-based time-series CV with purge + (optional) embargo and
    **balanced** train/test sizes.

    This cross-validator:

    1. Takes *regime* definitions (id, start_date, end_date).
    2. Generates all temporally valid combinations where test regimes are
       strictly after training regimes.
    3. Applies a purge period before the first test regime, removing the
       last `purge_period` days from the *end* of the training region.
    4. Scores each split by how close its train/test length ratio is to
       a target ratio.
    5. Keeps only the `max_splits` most balanced splits.

    Notes
    -----
    - Regimes are defined in *calendar days*; your actual data can be
      daily, weekly, etc. Dates are filtered via inequalities against the
      datetime level in the MultiIndex when the **ordering is first inferred**.
    - After the first call to `split` with a pandas object that has the
      datetime level, this class caches:
         * `self._dates_`  – per-row timestamps
         * `self._groups_` – per-row regime IDs
      and you can subsequently call `split` with numpy arrays (or any
      array-like) of the same length, without needing the index.

    Parameters
    ----------
    regimes : Sequence[tuple | dict]
        Iterable of regime definitions. Each element is either
        `(regime_id, start_date, end_date)` or a dict with keys
        `{"regime_id", "start_date", "end_date"}`.
    purge_period : int, default 1
        Number of **calendar days** to remove from the training region
        immediately before the *first* test regime's start date.
    embargo_period : int, default 1
        Number of **calendar days** after each test regime's end date
        that are excluded from training.
    datetime_level : str, default "date"
        Name of the datetime level in the pandas MultiIndex.
    ticker_level : str, default "ticker"
        Name of the ticker level (unused in the splitting logic but
        validated for clarity).
    max_test_regimes : int, default 3
        Maximum number of regimes included in a single test fold.
    max_splits : int, default 10
        Maximum number of folds to keep (the most balanced ones).
    target_train_test_ratio : float or None, default None
        Desired train/test size ratio (in **days**). If None, the median
        ratio of all valid regime combinations is used as the target.
    """

    def __init__(
        self,
        regimes: Sequence[Any],
        purge_period: int = 1,
        embargo_period: int = 1,
        datetime_level: str = "date",
        ticker_level: str = "ticker",
        max_test_regimes: int = 3,
        max_splits: int = 10,
        target_train_test_ratio: Optional[float] = None,
    ) -> None:
        self.regimes_raw = list(regimes)
        self.purge_period = int(purge_period)
        self.embargo_period = int(embargo_period)
        self.datetime_level = datetime_level
        self.ticker_level = ticker_level
        self.max_test_regimes = int(max_test_regimes)
        self.max_splits = int(max_splits) if max_splits is not None else None
        self.target_train_test_ratio = (
            float(target_train_test_ratio)
            if target_train_test_ratio is not None
            else None
        )

        # Normalised regimes
        self.processed_regimes: List[RegimeDefinition] = self._postprocess_regimes()

        # All valid temporal splits (before pruning)
        self._all_splits: List[Dict[str, Any]] = self._generate_all_splits()
        if not self._all_splits:
            raise ValueError("No valid regime splits could be generated.")

        # Prune to the most balanced ones
        if self.max_splits is not None and self.max_splits < len(self._all_splits):
            self._selected_splits = self._select_most_balanced_splits(
                self._all_splits, self.max_splits
            )
        else:
            self._selected_splits = list(self._all_splits)

        # --- NEW: internal cache so we can work with numpy arrays ---
        self._dates_: Optional[pd.DatetimeIndex] = None  # per-sample timestamps
        self._groups_: Optional[np.ndarray] = None        # per-sample regime_id
        self._n_samples_: Optional[int] = None

    # ------------------------------------------------------------------
    # Regime preparation
    # ------------------------------------------------------------------
    def _postprocess_regimes(self) -> List[RegimeDefinition]:
        """Normalise and de-overlap regimes.

        Overlaps are removed by *pushing forward* the start date of later
        regimes so they begin the day after the previous regime's end.
        Regimes that end up with non-positive duration are dropped.
        """

        if not self.regimes_raw:
            raise ValueError("`regimes` must be a non-empty sequence.")

        # Convert everything into RegimeDefinition objects
        regime_objs = [RegimeDefinition.from_any(r) for r in self.regimes_raw]

        # Sort by start date
        regime_objs.sort(key=lambda r: r.start_date)

        processed: List[RegimeDefinition] = []
        for reg in regime_objs:
            start, end = reg.start_date, reg.end_date

            # Adjust start to avoid overlaps with previous regimes
            for prev in processed:
                if start <= prev.end_date:
                    start = prev.end_date + timedelta(days=1)

            if start <= end:
                processed.append(
                    RegimeDefinition(
                        regime_id=reg.regime_id,
                        start_date=start,
                        end_date=end,
                    )
                )

        if not processed:
            raise ValueError("All regimes were filtered out after overlap removal.")

        return processed

    # ------------------------------------------------------------------
    # Split generation + scoring (regime-level, independent of X)
    # ------------------------------------------------------------------
    @staticmethod
    def _regime_duration_days(reg: RegimeDefinition) -> int:
        return int((reg.end_date - reg.start_date).days) + 1

    def _calculate_train_data_size(
        self,
        train_candidates: Sequence[RegimeDefinition],
        test_regimes: Sequence[RegimeDefinition],
    ) -> int:
        """Total training days for a candidate split.

        Training regimes are truncated so that they end `purge_period`
        days before the *earliest* test regime starts.
        """

        if not train_candidates or not test_regimes:
            return 0

        first_test_start = min(r.start_date for r in test_regimes)

        total_days = 0
        for reg in train_candidates:
            train_start = reg.start_date
            # Last inclusive train day after applying purge
            train_end = min(
                reg.end_date,
                first_test_start - timedelta(days=self.purge_period + 1),
            )
            if train_start <= train_end:
                total_days += int((train_end - train_start).days) + 1

        return total_days

    def _calculate_test_data_size(
        self, test_regimes: Sequence[RegimeDefinition]
    ) -> int:
        """Total number of test days for a candidate split."""
        return sum(self._regime_duration_days(reg) for reg in test_regimes)

    def _generate_all_splits(self) -> List[Dict[str, Any]]:
        """Enumerate all temporally valid train/test combinations.

        For a split point `k`, regimes `[0..k-1]` are *candidates* for
        training and `[k..end]` are *candidates* for testing. Test regimes
        are chosen as consecutive combinations starting at some index
        ≥ `k`, up to `max_test_regimes` in size.
        """

        regs = self.processed_regimes
        n = len(regs)

        if n < 2:
            # Need at least one train regime and one test regime
            return []

        all_splits: List[Dict[str, Any]] = []

        for split_point in range(1, n):
            train_candidates = regs[:split_point]
            test_candidates = regs[split_point:]

            max_test = min(self.max_test_regimes, len(test_candidates))
            for test_size in range(1, max_test + 1):
                # consecutive test regimes
                for start_idx in range(0, len(test_candidates) - test_size + 1):
                    test_regimes = test_candidates[start_idx : start_idx + test_size]

                    train_days = self._calculate_train_data_size(
                        train_candidates, test_regimes
                    )
                    if train_days <= 0:
                        continue

                    test_days = self._calculate_test_data_size(test_regimes)
                    if test_days <= 0:
                        continue

                    all_splits.append(
                        {
                            "train_regimes": list(train_candidates),
                            "test_regimes": list(test_regimes),
                            "split_point": split_point,
                            "train_days": int(train_days),
                            "test_days": int(test_days),
                        }
                    )

        return all_splits

    def _select_most_balanced_splits(
        self, all_splits: Sequence[Dict[str, Any]], max_splits: int
    ) -> List[Dict[str, Any]]:
        """Select up to `max_splits` splits whose train/test ratio is
        closest to `target_train_test_ratio` (in log space)."""

        if len(all_splits) <= max_splits:
            return list(all_splits)

        train_days_arr = np.array([s["train_days"] for s in all_splits], dtype=float)
        test_days_arr = np.array([s["test_days"] for s in all_splits], dtype=float)

        # Avoid division-by-zero (shouldn't happen because we filter > 0)
        test_days_arr = np.maximum(test_days_arr, 1.0)

        ratios = train_days_arr / test_days_arr

        if self.target_train_test_ratio is not None:
            target = float(self.target_train_test_ratio)
        else:
            # Use median observed ratio as target if none provided
            target = float(np.median(ratios))

        # Enforce sane limits on target
        if not np.isfinite(target) or target <= 0:
            target = 1.0

        balance_scores = np.abs(np.log(ratios / target))
        order = np.argsort(balance_scores)

        selected = [all_splits[i] for i in order[:max_splits]]
        return selected

    # ------------------------------------------------------------------
    # NEW: internal dates/groups cache, to allow numpy inputs later
    # ------------------------------------------------------------------
    def _ensure_dates_and_groups(self, X: Any) -> None:
        """
        Ensure that self._dates_ and self._groups_ are populated.

        First call must provide a pandas object with a MultiIndex that
        includes `datetime_level`, so we can infer ordering and regimes.

        Subsequent calls can pass numpy arrays (or anything with len(X)),
        as long as the length matches the cached `_n_samples_`.
        """
        n_samples = len(X)

        # If already cached, just sanity-check length
        if self._dates_ is not None:
            if n_samples != self._n_samples_:
                raise ValueError(
                    f"Length mismatch: previous data length was {self._n_samples_}, "
                    f"but new X has length {n_samples}. "
                    "To reuse this CV with a different ordering/length, "
                    "you need a new BalancedRegimePurgedEmbargoCV instance."
                )
            return

        # No cache yet: we must infer from a pandas index
        if not isinstance(X, (pd.DataFrame, pd.Series)):
            raise ValueError(
                "First call to `split` for BalancedRegimePurgedEmbargoCV must be "
                "with a pandas DataFrame/Series whose index has a datetime level "
                f"named '{self.datetime_level}'. After that, you can pass numpy "
                "arrays of the same length."
            )

        index = X.index
        if not isinstance(index, pd.MultiIndex):
            raise ValueError(
                "When inferring dates/groups, X.index must be a MultiIndex "
                "with at least a datetime level."
            )
        if self.datetime_level not in index.names:
            raise ValueError(
                f"datetime_level '{self.datetime_level}' not found in index names: "
                f"{index.names}"
            )

        # Per-row timestamps
        dates = pd.to_datetime(index.get_level_values(self.datetime_level))
        self._dates_ = pd.DatetimeIndex(dates)
        self._n_samples_ = n_samples

        # Per-row regime id (group) — this is the "internal groups" array
        groups = np.full(n_samples, fill_value=None, dtype=object)
        for reg in self.processed_regimes:
            mask = (self._dates_ >= reg.start_date) & (self._dates_ <= reg.end_date)
            groups[mask] = reg.regime_id
        self._groups_ = groups

    def get_groups(self) -> Optional[np.ndarray]:
        """
        Return the cached per-sample regime IDs (or None if not initialised yet).

        This is equivalent to the `groups` vector you would otherwise have to
        build and pass manually to `.split(groups=...)`.
        """
        return self._groups_

    # ------------------------------------------------------------------
    # scikit-learn API
    # ------------------------------------------------------------------
    def get_n_splits(self, X: Any = None, y: Any = None, groups: Any = None) -> int:
        return len(self._selected_splits)

    def split(self, X: Any, y: Any = None, groups: Any = None):
        """Yield train/test index arrays for each selected split.

        Parameters
        ----------
        X : array-like or pandas object
            - On the **first** call, must be a pandas DataFrame/Series with a
              MultiIndex containing `datetime_level`, so that we can infer the
              temporal ordering and map rows to regimes.
            - On **subsequent** calls, can be a numpy array (or any array-like)
              of the same length; the cached dates/groups will be reused.
        """

        # Make sure we have per-row dates & groups cached
        self._ensure_dates_and_groups(X)

        dates = self._dates_
        n_samples = self._n_samples_
        idx_positions = np.arange(n_samples)

        for split_info in self._selected_splits:
            train_regimes: List[RegimeDefinition] = split_info["train_regimes"]
            test_regimes: List[RegimeDefinition] = split_info["test_regimes"]

            # 1) Build test mask
            test_mask = np.zeros(n_samples, dtype=bool)
            for reg in test_regimes:
                mask = (dates >= reg.start_date) & (dates <= reg.end_date)
                test_mask |= mask

            if not test_mask.any():
                continue

            # 2) Build train mask with purge before earliest test
            first_test_start = min(r.start_date for r in test_regimes)
            train_mask = np.zeros(n_samples, dtype=bool)

            for reg in train_regimes:
                train_start = reg.start_date
                train_end = min(
                    reg.end_date,
                    first_test_start - timedelta(days=self.purge_period + 1),
                )
                if train_start > train_end:
                    continue
                mask = (dates >= train_start) & (dates <= train_end)
                train_mask |= mask

            # 3) Apply embargo after each test regime (if any)
            if self.embargo_period > 0:
                embargo_mask = np.zeros(n_samples, dtype=bool)
                for reg in test_regimes:
                    emb_start = reg.end_date + timedelta(days=1)
                    emb_end = emb_start + timedelta(days=self.embargo_period)
                    mask = (dates >= emb_start) & (dates <= emb_end)
                    embargo_mask |= mask

                train_mask &= ~embargo_mask

            # Final indices
            train_idx = idx_positions[train_mask]
            test_idx = idx_positions[test_mask]

            if len(train_idx) == 0 or len(test_idx) == 0:
                continue

            yield train_idx, test_idx

