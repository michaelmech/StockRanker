def derive_spearman_eigenvector_matrix(
    df: pd.DataFrame,
    ticker_level: str = "ticker",
    date_level: str = "date",
    value_cols=None,
    use_abs: bool = True,
    scale_by_eigenvalue: bool = False,
) -> pd.DataFrame:
    """
    For each date, compute the Spearman correlation matrix between tickers
    using the feature vectors at that date, then extract the leading eigenvector.

    Assumes df has a MultiIndex with levels including `ticker_level` and `date_level`,
    and columns are features.

    For each date:
        - Take sub-DF of shape (n_tickers, n_features)
        - Compute corr = sub.T.corr('spearman')  -> (n_tickers x n_tickers)
        - Compute leading eigenvector of corr
    Stack these eigenvectors into a DataFrame of shape (n_dates, n_tickers).

    Parameters
    ----------
    df : pd.DataFrame
        MultiIndex DataFrame indexed by (ticker, date) in some order.
    ticker_level : str, default "ticker"
        Name of the ticker index level.
    date_level : str, default "date"
        Name of the date index level.
    value_cols : list or None, default None
        List of feature columns to use. If None, all numeric columns are used.
    use_abs : bool, default True
        Take absolute value of eigenvector components (since sign is arbitrary).
    scale_by_eigenvalue : bool, default False
        If True, scale the eigenvector by its leading eigenvalue.

    Returns
    -------
    pd.DataFrame
        Index: dates
        Columns: union of all tickers seen across dates
        Values: leading eigenvector loadings per (date, ticker).
        Missing ticker on a date ⇒ NaN.
    """
    if not isinstance(df.index, pd.MultiIndex):
        raise ValueError("df must have a MultiIndex with ticker and date levels.")

    if ticker_level not in df.index.names or date_level not in df.index.names:
        raise ValueError(
            f"MultiIndex must contain levels '{ticker_level}' and '{date_level}'. "
            f"Got levels: {df.index.names}"
        )

    # Choose feature columns
    if value_cols is None:
        value_cols = df.select_dtypes(include=[np.number]).columns.tolist()
    else:
        missing = set(value_cols) - set(df.columns)
        if missing:
            raise ValueError(f"value_cols missing in df.columns: {missing}")

    # Sort for stable grouping
    df = df.sort_index(level=[date_level, ticker_level])

    results = []

    # Group by date; each `sub` has rows = tickers, columns = features
    for date, sub in df.groupby(level=date_level):
        # Keep only feature columns
        sub = sub[value_cols]

        # Drop rows/cols that are all NaN
        sub = sub.dropna(how="all", axis=0)  # drop tickers with no data
        sub = sub.dropna(how="all", axis=1)  # drop features with no data

        # Need at least 2 tickers and 2 features to get a meaningful correlation
        if sub.shape[0] < 2 or sub.shape[1] < 2:
            continue

        # sub: (n_tickers, n_features)
        # We want corr between tickers using features as observations:
        #   variables = tickers, observations = features
        # => sub.T: (n_features, n_tickers), corr() along columns => (n_tickers x n_tickers)
        corr = sub.T.corr(method="spearman")

        # Drop tickers with entirely NaN correlations (degenerate)
        corr = corr.dropna(axis=0, how="all").dropna(axis=1, how="all")
        if corr.shape[0] < 2:
            continue

        C = corr.values.astype(float)

        # Handle any remaining NaNs in corr
        if np.isnan(C).any():
            # Set diagonal to 1, off-diagonal NaNs -> 0
            np.fill_diagonal(C, 1.0)
            C = np.nan_to_num(C, nan=0.0)

        # Symmetric matrix: use eigh
        eigenvals, eigenvecs = np.linalg.eigh(C)
        idx_max = np.argmax(eigenvals)
        leading_vec = eigenvecs[:, idx_max]

        if use_abs:
            leading_vec = np.abs(leading_vec)

        if scale_by_eigenvalue:
            leading_vec = leading_vec * eigenvals[idx_max]

        tickers = corr.index
        s = pd.Series(leading_vec, index=tickers, name=date)
        results.append(s)

    if not results:
        return pd.DataFrame()

    # Stack into DataFrame: rows = dates, columns = union of tickers
    if not results:
        return pd.DataFrame()

    # Stack into DataFrame, aligning by ticker (columns) and stacking by date (rows)
    # The index names (dates) and Series index (tickers) will be used correctly.
    out = pd.concat(results, axis=0)

    # Since `results` is a list of Series, pd.concat(..., axis=0) will result in a
    # Series with a MultiIndex of (date, ticker). We need to unstack to get
    # the final (date x ticker) DataFrame.
    out = out.unstack(level=-1)

    # Sort by date
    out = out.sort_index()
    return out.T

