class CombinatorialPurgedEmbargoCV(BaseCrossValidator):
    def __init__(self,
                 n_blocks=10,
                 test_size_n_blocks=2,
                 purge_gap_n_blocks=1,
                 embargo_gap_n_blocks=1,
                 datetime_level='date',
                 ticker_level='ticker',
                 max_splits=None,
                 groups=None,
                 random_state=None):
        self.n_blocks = n_blocks
        self.test_size_n_blocks = test_size_n_blocks
        self.purge_gap_n_blocks = purge_gap_n_blocks
        self.embargo_gap_n_blocks = embargo_gap_n_blocks
        self.datetime_level = datetime_level
        self.ticker_level = ticker_level
        self.max_splits = max_splits
        self.random_state = random_state
        self.groups = groups

    def get_n_splits(self, X=None, y=None, groups=None):
        # Number of possible ways to choose test_size_n_blocks blocks from the last n_blocks-(min_train_blocks+purge_gap) blocks
        # We'll compute this dynamically in the split method
        return self.max_splits if self.max_splits else self.n_blocks - self.test_size_n_blocks + 1


    def _extract_dates(self, X, groups):
        """Return unique dates and sample-level dates."""

        # CASE 1: DataFrame with MultiIndex → ignore groups
        if isinstance(X, pd.DataFrame) and isinstance(X.index, pd.MultiIndex):
            sample_dates = X.index.get_level_values(self.datetime_level).to_numpy()
            dates_unique = np.unique(sample_dates)
            return dates_unique, sample_dates

        # CASE 2: Use provided groups (manual or from init)
        if groups is None:
            raise ValueError(
                "No MultiIndex present and no `groups` provided. "
                "Pass groups at init or as split(..., groups=...)."
            )

        sample_dates = np.asarray(groups)
        if sample_dates.shape[0] != len(X):
            raise ValueError("groups must have same length as X.")

        dates_unique = np.unique(sample_dates)
        return dates_unique, sample_dates

    def split(self, X, y=None, groups=None):
        # groups argument overrides the init-set groups
        groups = groups if groups is not None else self.groups

        dates, sample_dates = self._extract_dates(X, groups)

        dates = X.index.get_level_values(self.datetime_level).unique()
        dates = np.sort(dates)
        total_dates = len(dates)

        block_size = total_dates // self.n_blocks
        blocks = [dates[i*block_size : (i+1)*block_size] for i in range(self.n_blocks - 1)]
        blocks.append(dates[(self.n_blocks - 1)*block_size:])

        # Generate chronologically correct splits using a sliding window approach
        valid_splits = []

        # Min train blocks is 1 (we need at least one block for training)
        min_train_blocks = 1

        # Iterate through possible test window starting positions
        # Start at min_train_blocks to ensure we have at least that many blocks for training
        for test_start_idx in range(min_train_blocks, self.n_blocks - self.test_size_n_blocks + 1):
            # End index is exclusive, so add test_size_n_blocks
            test_end_idx = test_start_idx + self.test_size_n_blocks

            # Test blocks are consecutive from start to end
            test_blocks = list(range(test_start_idx, test_end_idx))

            # All blocks before test_start_idx minus purge gap are available for training
            train_start_idx = 0
            # We exclude blocks that are within purge gap before test
            train_end_idx = max(0, test_start_idx - self.purge_gap_n_blocks)

            train_blocks = list(range(train_start_idx, train_end_idx))

            # Skip if no training blocks
            if not train_blocks:
                continue

            valid_splits.append((train_blocks, test_blocks))

        if not valid_splits:
            raise ValueError(
                "Cannot create valid train-test splits with the given parameters. "
                "Try reducing test_size_n_blocks, purge_gap_n_blocks, or increasing n_blocks."
            )

        # Select splits if max_splits is specified
        if self.max_splits and self.max_splits < len(valid_splits):
            rng = check_random_state(self.random_state)
            indices = rng.choice(len(valid_splits), size=self.max_splits, replace=False)
            selected_splits = [valid_splits[i] for i in indices]
        else:
            selected_splits = valid_splits

        # Generate train/test indices for each split
        for train_blocks, test_blocks in selected_splits:
            # Apply embargo (blocks after test)
            test_blocks_set = set(test_blocks)
            last_test_block = max(test_blocks)

            embargo_blocks = set(range(
                last_test_block + 1,
                min(self.n_blocks, last_test_block + 1 + self.embargo_gap_n_blocks)
            ))

            # Training blocks exclude test and embargo
            train_blocks = [b for b in train_blocks if b not in embargo_blocks]

            # Skip if no training blocks remain after embargo
            if not train_blocks:
                continue

            # Convert blocks to dates
            train_dates = np.concatenate([blocks[i] for i in train_blocks])
            test_dates = np.concatenate([blocks[i] for i in test_blocks])

            # Get indices from data
            index = X.index
            train_mask = index.get_level_values(self.datetime_level).isin(train_dates)
            test_mask = index.get_level_values(self.datetime_level).isin(test_dates)

            train_idx = np.where(train_mask)[0]
            test_idx = np.where(test_mask)[0]

            if len(train_idx) == 0 or len(test_idx) == 0:
                continue

            yield train_idx, test_idx


