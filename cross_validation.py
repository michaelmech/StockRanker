class SlidingWindowPurgedEmbargoCV(BaseCrossValidator):
    """
    Sliding window cross-validation for time series with purge and embargo gaps.

    This cross-validator provides train/test indices to split time series data samples
    that are observed at fixed time intervals according to a sliding window scheme.
    Both training and testing windows have fixed sizes, and each fold's test set
    immediately follows its training set in time, respecting purge and embargo gaps.

    Parameters
    ----------
    train_size_n_blocks : int
        Number of blocks to include in each training set.
    test_size_n_blocks : int
        Number of blocks to include in each testing set.
    step_n_blocks : int, default=1
        Number of blocks to step forward between each fold.
    purge_gap_n_blocks : int, default=0
        Number of blocks to exclude from training that immediately precede the test set.
    embargo_gap_n_blocks : int, default=0
        Number of blocks to exclude from future training sets that immediately follow the test set.
    datetime_level : str, default='date'
        Name of datetime level in the pandas MultiIndex.
    ticker_level : str, default='ticker'
        Name of ticker level in the pandas MultiIndex.
    n_splits : int, default=None
        Maximum number of splits. If None, generate all possible splits.
    random_state : int, RandomState instance or None, default=None
        Controls the random selection of folds when n_splits is specified.
        Pass an int for reproducible output.
    """
    def __init__(self,
                 train_size_n_blocks,
                 test_size_n_blocks,
                 step_n_blocks=1,
                 purge_gap_n_blocks=0,
                 embargo_gap_n_blocks=0,
                 datetime_level='date',
                 ticker_level='ticker',
                 n_splits=None,
                 groups=None,
                 random_state=None):
        self.train_size_n_blocks = train_size_n_blocks
        self.test_size_n_blocks = test_size_n_blocks
        self.step_n_blocks = step_n_blocks
        self.purge_gap_n_blocks = purge_gap_n_blocks
        self.embargo_gap_n_blocks = embargo_gap_n_blocks
        self.datetime_level = datetime_level
        self.ticker_level = ticker_level
        self.n_splits = n_splits
        self.random_state = random_state
        self.groups=groups

    def get_n_splits(self, X=None, y=None, groups=None):
      """
      Returns the number of splitting iterations in the cross-validator.
      """
      if X is not None:
          # Use groups passed here or fall back to groups set at init
          groups_eff = groups if groups is not None else getattr(self, "groups", None)
          dates, _ = self._extract_dates(X, groups_eff)
          total_dates = len(dates)

          # Each date is a block in this implementation
          n_blocks = total_dates
      else:
          # Without X, we can't calculate number of splits; fall back to n_splits
          return self.n_splits if self.n_splits is not None else 0

      min_blocks_needed = (self.train_size_n_blocks +
                          self.purge_gap_n_blocks +
                          self.test_size_n_blocks)

      if min_blocks_needed > n_blocks:
          return 0

      possible_splits = 1 + (n_blocks - min_blocks_needed) // self.step_n_blocks

      if self.n_splits is not None:
          return min(self.n_splits, possible_splits)
      return possible_splits


    def _extract_dates(self, X, groups):
      """
      Return:
          dates_unique_sorted: np.ndarray of unique sorted dates (per-block units)
          sample_dates: np.ndarray of date per sample, same length as X
      """
      # Case 1: DataFrame with MultiIndex → use datetime level, ignore groups
      if isinstance(X, pd.DataFrame) and isinstance(X.index, pd.MultiIndex):
          sample_dates = X.index.get_level_values(self.datetime_level).to_numpy()
          dates_unique = np.unique(sample_dates)
          return dates_unique, sample_dates

      # Case 2: Any other X → require groups (from init or split)
      if groups is None:
          raise ValueError(
              "No MultiIndex present and no `groups` provided. "
              "Pass groups at init or as split(..., groups=...)."
          )

      sample_dates = np.asarray(groups)
      if sample_dates.shape[0] != len(X):
          raise ValueError("`groups` must have same length as X.")

      dates_unique = np.unique(sample_dates)
      return dates_unique, sample_dates


    def split(self, X, y=None, groups=None):

        # Use groups passed here or fall back to groups set at init
        groups_eff = groups if groups is not None else getattr(self, "groups", None)

        # Get unique dates and per-sample dates
        dates, sample_dates = self._extract_dates(X, groups_eff)
        dates = np.sort(dates)
        total_dates = len(dates)

        # Each date is a block
        n_blocks = total_dates
        block_size = 1

        # Create blocks (each date is its own block)
        blocks = [[date] for date in dates]


        # Generate all possible sliding windows
        valid_splits = []

        # Total blocks needed for each split
        min_blocks_needed = (self.train_size_n_blocks +
                            self.purge_gap_n_blocks +
                            self.test_size_n_blocks)

        # Check if we have enough blocks
        if min_blocks_needed > n_blocks:
            raise ValueError(
                f"Not enough blocks ({n_blocks}) for the requested window sizes "
                f"(train: {self.train_size_n_blocks}, purge: {self.purge_gap_n_blocks}, "
                f"test: {self.test_size_n_blocks})."
            )

        # Generate all possible sliding windows
        for start_idx in range(0, n_blocks - min_blocks_needed + 1, self.step_n_blocks):
            train_start = start_idx
            train_end = start_idx + self.train_size_n_blocks

            # Account for purge gap
            test_start = train_end + self.purge_gap_n_blocks
            test_end = test_start + self.test_size_n_blocks

            # Check if test_end exceeds n_blocks
            if test_end > n_blocks:
                break

            valid_splits.append((
                list(range(train_start, train_end)),  # Train blocks
                list(range(test_start, test_end))     # Test blocks
            ))

        if not valid_splits:
            raise ValueError(
                "Cannot create valid train-test splits with the given parameters."
            )

        # Select splits if n_splits is specified
        if self.n_splits and self.n_splits < len(valid_splits):
            rng = check_random_state(self.random_state)
            indices = rng.choice(len(valid_splits), size=self.n_splits, replace=False)
            selected_splits = [valid_splits[i] for i in indices]
        else:
            selected_splits = valid_splits

        # Apply embargo: keep track of embargooed dates for future folds
        embargooed_dates = set()

        # Process each split
        for train_blocks, test_blocks in selected_splits:
            # Get the dates for train and test
            train_dates = np.concatenate([blocks[i] for i in train_blocks])
            test_dates = np.concatenate([blocks[i] for i in test_blocks])

            # Calculate embargo blocks for future iterations
            if self.embargo_gap_n_blocks > 0:
                embargo_end = min(n_blocks, test_blocks[-1] + 1 + self.embargo_gap_n_blocks)
                embargo_blocks = list(range(test_blocks[-1] + 1, embargo_end))
                embargo_dates = np.concatenate([blocks[i] for i in embargo_blocks]) if embargo_blocks else []
                embargooed_dates.update(embargo_dates)

            # Remove embargooed dates from training
            train_dates = [d for d in train_dates if d not in embargooed_dates]

            # Skip if no training dates remain after embargo
            if len(train_dates) == 0:
                continue


            # Get indices from data using sample_dates instead of X.index
            train_mask = np.isin(sample_dates, train_dates)
            test_mask = np.isin(sample_dates, test_dates)

            train_idx = np.where(train_mask)[0]
            test_idx = np.where(test_mask)[0]


            if len(train_idx) == 0 or len(test_idx) == 0:
                continue

            yield train_idx, test_idx

