def compute_fold_score(estimator, X, y, train_idx, test_idx):
    """
    Compute Spearman correlation for a single fold.

    Parameters:
    - estimator: scikit-learn compatible estimator
    - X: pd.DataFrame of features
    - y: pd.Series or array-like target values
    - train_idx: indices for training set
    - test_idx: indices for test set

    Returns:
    - Spearman correlation coefficient
    """
    # Clone estimator to ensure independence across folds
    estimator = clone(estimator)

    # Extract training and test sets using numpy arrays for efficiency
    X_train, X_test = X.values[train_idx], X.values[test_idx]
    y_train, y_test = y.values[train_idx], y.values[test_idx]

    # Fit and predict
    estimator.fit(X_train, y_train)
    y_pred = estimator.predict(X_test)

    # Compute Spearman correlation
    corr, _ = spearmanr(y_test, y_pred)
    return corr

